	<h1>Path to Purchase / Journey Mapping</h1>	

<p>The Path to Purchase is the series of channels that a customer uses, or are exposed to, that result in a purchase. These channels include emails, apps, search engines, websites, loyalty programs, review sources and social networks. The goal is to ensure every point of contact on the customer’s journey is ready to be activated instantly.</p>
	
	<p>The customer journey refers to the experience consumers have when they make a purchase. This is the customer’s actions within these channels that influence them to either proceed or drop off from the journey they are on. In the intent-driven moments of decision making throughout the customer journey, marketers can note their efforts and track the patterns within the customer journey to improve the overall purchasing experience.</p>
	
	<p>In order for businesses to be more strategic about where and when to reach out to their consumers, they need to have a better understanding of their customer’s full journey and its timeline.  Journey mapping creates a visual story of your customers' interactions with your brand. It starts by compiling a series of the consumer’s actions into a timeline. The timeline uses the consumer’s thoughts and emotions to create a story. This story is edited and refined, ultimately leading to a visualization. </p>
