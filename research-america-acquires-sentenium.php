		<b><em>Market Research Company Expands in California and Enters Asian Markets </em></b>




<p>Newtown Square, Pennsylvania, October 2, 2017. Research America, Inc. announced it had purchased Sentenium, Inc. The acquired business, located in Pleasant Hill, California, is a global marketing research supplier. This acquisition adds to Research America's operations in the state.
<br>

Research America Sacramento has provided regional support in California since 1990. This strategic procurement also marks Research America’s first offshore expansion. Sentenium’s operations include a Chinese mainland office, located in Zhongshan. </p>

		<p>“Sentenium’s people are its greatest resource,” commented Porter. "They understand how businesses develop multicultural consumer and brand relationships. Working together, we provide comprehensive studies for clients in California and across Asia." </p>

<p>Research America is changing the marketing research industry. Robert Porter, CEO, and Rex Repass, President, are expanding the privately-held corporation. Their expansion plan includes opening offices strategically located along major US business corridors. Each acquisition builds new capabilities into Research America-- abilities that clients want. 
</p>

		<p>Sentenium launched independently from its parent human resources company, People Focus, in 2004. The business developed service lines in survey research, design, printing, data processing, and translation. Sentenium now specializes in offering on-demand, comprehensive marketing, and marketing research solutions. It provides its services in more than seventy-nine languages to a global marketplace.</p>
 
	
