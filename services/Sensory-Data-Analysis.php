    	<h1>Sensory Data Analysis</h1>	
    
    	<p>Sensory analysis, also known as sensory evaluation, is the scientific way to measure, analyze and interpret reactions to characteristics of products as they are perceived by people’s sense of sight, smell, taste, touch and hearing.  Central Location Testing (CLT), Home-Usage Testing (HUT), Discrimination Testing, 60/40 Testing, and Descriptive Analysis are a few of the different methods Research America uses to collect this data</p>
        
        <p>Research America has <a href="../services/focus-group-facilities.php">state-of-the-art facilities</a>  and technology.  Our CLTs measure the consumer’s degree of liking for stimuli using a scaling system, which is an important component of product optimization, and can evaluate the following: </p>
        
        <img src="../_img/HUTS_CLTS.jpg" class="focus-group-facilities-1" alt="Market Research Focus Groups" style="height:100%;">
        <ul class="check">
            <li>Sequential monadic, Monadic, Protomonadic</li>
            <li>In booth evaluation of products</li>
            <li>In kitchen evaluation of products</li>
            <li>Concept evaluation</li>
            <li>Package evaluation</li>
            <li>Shelf set evaluation</li>
            <li>Activity based – exercise CLT, product prep CLT</li>
            <li>Exit attitude and behavior questions</li>
            <li>Price Value impressions</li>
            <li>Post taste focus groups</li>
            <li>Branded or blind</li>
        </ul>
        
        <p>HUTs allow the consumer to see, touch, and use the products as they typically would in their own environment and with their typical behavior.  Our in-home consumer evaluations of products are used to understand usage behavior, satisfactions, and frustrations about a product’s performance. This is a critical process for products that have a specific usage behavior and/or occasion and can include <a href="../services/qualitative-market-research.php">qualitative research</a> , <a href="../services/quantitative-data-collection.php">quantitative research</a> , online questionnaires, and videos. </p>
        <p><a href="product-improvement.php">Discrimination Testing</a> provides instant feedback to determine if there are any perceivable differences (or similarities) between products.  This helps your development team understand the opportunity risk – the risk that a difference <i>is</i> perceived when it <i>isn’t</i> there – and the franchise risk – the risk that a difference is <i>not</i> perceived when it <i>is</i> there.   Research America will partner with you to determine the appropriate size, method, and opportunity risk models that will achieve the best results.</p>
        
        <p>The main objective of 60/40 preference study is to achieve taste and nutritional superiority over competitors’ products in order to communicate the specifics of this superiority to consumers. The name of this tool comes from the desired result of a product-preference test. Two products are compared: one current product and one from a competitor.  Products are tested with consumer panels and at least 60 out of 100 people must prefer your product to achieve superiority over a competitor’s.  The desired result is that at least 60% of consumers prefer your product to the competitor product, hence 60/40.  This type of study is used in the following instances:</p>
        
        <ul class="check">
            <li>Benchmarking against competitor products</li>
            <li>Quality among production lines</li>
            <li>Product optimization (i.e. ingredient changes, sizes, shapes, etc.)</li>
            <li>New product development</li>
            <li>In combination with another key product (concept/package) understanding</li>
        </ul>
        
        <p>Descriptive Analysis is a behavioral approach that uses trained panels to measure a product’s sensory characteristics through everyday consumer language. The panel members use their senses to perceive product similarities and differences. It is used both to obtain qualitative descriptors of the product and quantitative evaluations of the product.  Results are representative of the larger user population.  </p>
        
        <p>This method provides the following:</p>
        <ul class="check">
            <li>A description of the major product sensory categories</li>
            <li>A detailed separation and description of each sensory attribute within each major sensory category</li>
            <li>The perceived intensity of each sensory attribute</li>
            <li>Statistical evaluation of the descriptive data</li>
        </ul>
        <p>And should be used for the following:</p>
        <ul class="check">
            <li>New product development</li>
            <li>Product Optimization</li>
            <li>Advertisement claim substantiation</li>
            <li>Category review</li>
            <li>Quality control / product drift</li>
            <li>Formulation and processing effects on finished product quality</li>
            <li>Correlation of descriptive analysis and consumer preference behavior</li>
        </ul>
        
