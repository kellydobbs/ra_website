	<h1>Marketing Research Telephone Interviews</h1>		
<p>	<img src="../_img/telephone-interview.jpg" class="focus-group"   alt="marketing research telephone interviews" />	

				<p>Research America designs <strong>marketing research telephone interview studies</strong> for both private sector businesses and public organizations. Our consultants deliver continuous and ad hoc projects for any organization.</p> 

	<p><strong>Telephone interviewing services </strong>fall into two basic categories, quantitative and qualitative:</p> 

	<p>Computer Assisted Telephone Interviewing (CATI) is primarily a quantitative data collection method, which uses a blend of live telephone interview and optional Interactive Voice Response (IVR) survey interviews to rapidly collect information from your targeted sample. CATI surveys often contain one or two open-ended questions asked by a live interviewer. Software systems immediately process results and perform initial analysis. Quota control is built-in to our systems: software actively tracks progress without the time lag which often occurs when interviews occur in the field.</p>

	<p>Telephone In-Depth Interviewing (Telephone IDI) involves qualitative research. A trained in-depth interviewer conducts an interview over the phone with a targeted respondent, using a topic guide instead of a structured questionnaire.  Telephone IDIs enable Research America’s interviewing team to reach more respondents than on-site or live face- to -face interviews allow logistically.</p>

	<p><strong>Advantages and Disadvantages Over In-Person Interviewing</strong></p>

	<p>Each type of marketing research interview has its benefits and drawbacks. CATI is fast, but impersonal. Telephone IDIs can reach more respondents across wider regions, but interviewers are unable to observe, respond to and report on respondents’ body language and other nonverbal cues which are helpful during an IDI. In any type of telephone interview, it’s harder to develop rapport with the respondent, which is helpful when study topic involves sensitive, or very personal questions.</p>

