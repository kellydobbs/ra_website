<h1>Improve UX Research with a Holistic Approach: 6 Key Questions to Ask </h1>

		<h2 class="green-ques">Wondering how consumers think and interact with your digital applications and products?</h2>
  

			<p>	<img src="../_img/UX-Research.jpg" class="focus-group-facilities-1" alt="Improve UX Research with a Holistic Approach"></p>

	<p>Steve Jobs once said, “If the user is having a problem, it’s our problem.” UX research analyzes users’ experiences to identify key insights and potential issues that will assist in product innovation, improve product performance and enhance brand credibility, ultimately resulting in consumer growth. A holistic approach helps you measure success through the eyes of the user. It is essential to go beyond functionality and aesthetics and start with the user first, and then move to their experience.  
</p>

	
	<br>
	
<h2>The holistic approach to UX research starts with six key questions:
</h2>

	
	
	<p>The holistic approach to UX research starts with six key questions:</p>
	
	
	<ol>
		<li>	<strong> 		Who? </strong><br>
<strong> Determine the prospective audiences for your design work.  </strong><br>Defining your customer personas (demographics, psychographics, needs, etc.) will help you understand how your design can facilitate their experiences.
 </li>
		<br>
		<li><strong> What? </strong><br>
Clarify what features people use in your website, application, or product, and what they want to accomplish. </strong> Understanding the user’s end game will help you design a product that will make it easier for users to reach their goals.
	</li>
		<br>
		
	  <li> <strong> When?</strong><br>
<strong>Determine when people  use particular products or technologies, as well as how those technologies and products fit into their daily routines.</strong> Keep in mind that extensive change in habits and practices can run a higher risk of rejection.</li>
		</br>

		
		
<li><strong>	Where? </strong> <br>
<b>Defining physical locations where people perform specific tasks and use  key technologies, including the devices are important guides to defining the optimal user experience. </b>Usage locations, preferred device, and end destination must be considered in user experience design.</li>
<br>
  
</li>

<li><strong>		Why? </strong> </br>
 <b>Explains the Underlying  emotional and rational drivers behind user behavior, and the reasons for that behavior.</b> Understanding the  motivations, emotions, and expectations behind user’s behavior are key in helping define new products and improvements.</li>
  
  </br>
  

<li><b>	How? </b> <br>
<b>Learn and document the explicit actions or steps people take to perform tasks or reach their goals.</b> Define where your product fits in the bigger picture.



  
</ol>
	

<p>Answering these six questions about your user experience, you will develop an intimate familiarity with your user audiences and a foundational consumer understanding that will uncover valuable insights that will result in development with a purpose, minimize timing, and ultimately a reduction in costs.</p>

