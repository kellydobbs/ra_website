	<h1>Research America Acquires Chicago’s APC Research, Inc.</h1>

	<h3>Chicago, Illinois, November 2, 2015</h3>
    
<p>	APC brings depth of knowledge in public opinion polling for major news publications, one of Research America’s
 areas of focus and growth. Research America supports both political clients and major media organizations through 
 its opinion telephone and web research capabilities, chiefly through its Sacramento, California office.
  The re-branded Research America Chicago location will continue to deliver opinion research for news clients, 
  political organizations and public research groups, as well as marketing research across various industries. </p>

<p>The center boasts a state of the art test kitchen featuring new appliances and product preparation work areas.
 Rooms can be customized to simulate restaurant and retail grocery environments for food and beverage testing 
 projects. New appliances in the test kitchen meet standards for both commercial (food service and restaurant)
  and consumer product testing. </p>

<p>Research America President Robert Porter comments: “The APC team has supported client research
 in and around Chicago for many years, and each member brings skills and wisdom to our company.
  We are looking forward to introducing Research America to clients in Chicago and the Midwest.” </p>
 
<p>Through this acquisition, APC will become part of Research America’s network of facilities currently based
 in Newtown Square and Philadelphia, PA; Lehighton and Pottsville, PA, Rochester, NY, Chicago, IL and Sacramento,
  CA. </p>


	

