 	  <h1>Research America Announces Cincinnati Expansion
</h1>
		


<p>
<b>Cincinnati, Ohio, November 13, 2018 </b></p>
<p>Research America Inc., a leading marketing research provider, announced it has hired all remaining Parker Insights employees. Parker, formally known as Parker Marketing Research, launched in Cincinnati in 1985 and closed in late 2017. </p>
<p>“We are pleased that we were able to hire several of Parker’s highly experienced employees,” according to Robert Porter, CEO of the Philadelphia-based company.  “At the center of our core values is to deliver attentive customer service along with superior value,&quot; Porter added. &quot;Our new employees in Cincinnati excel at developing innovative methods and tools to meet client needs.  The former Parker staff will be an asset to Research America's full-service team in Cincinnati.&quot; </p>
<p>Research America has been named to Inc. magazine’s list of fastest growing, privately held companies in America in both 2018 and 2017.  The company now has 14 locations coast-to-coast, including Philadelphia, New York, Orlando, Detroit, and Sacramento.</p>
	
  <p>Parker’s researchers are best known for having pioneered in-person, blended methodologies. Parker has combined the strongest elements of quantitative and qualitative techniques, all in a real-time research environment. By conceptualizing research in new ways, they have created superior value for clients by expanding learning, compressing project cycle times and significantly enhancing return on investment. Parker’s experience includes research in multiple business categories including consumer packaged goods, food and beverage, travel and hospitality, apparel and footwear, home and hardware, and retail. </p>
 
 
	
