 <p class="big-link">In-Depth Interviews (IDIs)</p>
<p>In-depth interviews with survey respondents are conducted either in-person or over the phone. IDIs are typically used during B2B interviews with respondents in high-level, or executive positions. The primary bene ts to using IDI research is access to hard-to-reach respondents and generating detailed data from candid conversations. Interview responses are used to describe over-arching attitudes and opinions, and they add depth when incorporated into descriptive analysis for larger studies.</p>

<p class="big-link">Face-to-Face</p>
<p>Face-to-Face interviews are often used to obtain information that may be too sensitive to collect using other survey techniques. The primary bene t of using face-to-face interviewing is having an interviewer present, which establishes trust on the part of the respondent. Additionally, an interviewer is able to ask clarifying questions and incorporate visual aids to obtain more descriptive results.</p>

<p class="big-link">Online Communities, Bulletin Boards, and Focus Groups</p>
Private, online forums where invited users can share information, ideas, and opinions. Respondents receive invitations to log-in to answer questions on their own schedules. We provide trained online moderators, and clients can
observe results and print transcripts at any point during the study. These qualitative technologies bring the interactive advantages of focus groups or face-to-face IDIs to online research.</p>
<p class="big-link">Moderating</p>
<p>Expert moderators independently probe respondents leading them through a nuanced pre-designed discussion guide. All Research America moderators are professionally trained and experienced, many with advanced degrees and over 15 plus years of experience in multiple disciplines such as pharmaceutical, healthcare, banking, packaged goods, IT, retail, agriculture, automotive, and many more.</p>
<p class="big-link"> Focus Groups</p>
Focus groups are moderated conversations, typically conducted with 10-12 participants. While the research questions are scripted, responses are not. Moderators guide participants in a natural conversation, leveraging projective techniques about the subject of the study. It is recommended that individual opinions or ideas expressed in a study be used to describe a widely held opinion, and in some cases, the answers respondents provide may trigger new insights or ideas as part of a larger study.

