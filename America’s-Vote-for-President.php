	  Health and Wellness Issues Loom Large in America’s Vote for President</h1>
	<p><b> Newtown Square, PA </b> –– With 10% of Americans reporting COVID-19 symptoms, the federal management of the virus pandemic is a growing concern. Voters are also casting a critical eye on the current administration’s healthcare policies over the last 4 years. </p>
	 <p> Before Trump took office, 12% of the American adult population reported the need to lose weight for health reasons, but now 27% say weight is a health concern for them.  According to mid-October 2020 consumer research by Natural Marketing Institute (NMI), the rise in such health conditions may indicate why most of the Institute’s Health &amp; Wellness segments lean towards a Biden presidency and progressive platforms. The Institute’s Health &amp; Wellness segmentation model is based on extensive custom, quantitative consumer interviews of more than 100,000 American adults. </p>
	
	<p>  Based on the Institute’s extensive consumer research, the most health proactive segment of the American population (WELL BEINGS), voiced enthusiastic support for Biden (60% say Biden/Harris will receive their vote – almost double that of the Trump/Pence likely voters). Their support of this ticket is followed by the FENCE SITTERS segment, America’s health strivers, 50% of whom say they will cast their ballot for Biden. </p>
	 <p> According to Steve French, senior vice president, “these two segments combined create a powerful coalition in American politics. Together they account for 49% of the adult population, up 5 percentage points from 2016, and their support for the Democratic nominee has increased since the last presidential campaign.” French also notes that “in 2016, 51% of WELL BEINGS and 44% of FENCE SITTERS said they voted for Clinton.”</p>
	 <p> While the trends of party support among the Institute’s Health and Wellness segments have not drastically changed over the last 4 years, the political polarization of segments has increased. WELL BEINGS and FENCE SITTERS are now more likely to identify as Democrats (50% and 42% respectively) than in 2016 (43% and 40%). FOOD ACTIVES, an older more conservative segment, have increased their party affiliation with Republicans from 37% in 2016 to 41% in 2020.</p>
	 <p> During the 2016 election, the other two Health &amp; Wellness segments (MAGIC BULLETS and EAT, DRINK &amp; BE MERRYS) did not particularly favor either candidate.  This year the percent of both groups who say they will not vote has dropped.  MAGIC BULLETS continue to be ambivalent about candidates, but EAT, DRINK &amp; BE MERRYS favor Biden over Trump by 11 percentage points – the same lead within the general population.</p>
