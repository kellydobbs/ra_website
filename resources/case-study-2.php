	<h2>The Client</h2>		
 <p> A small, start-up tea brand that had been recently acquired by a larger company.</p>

<h2>The Problem</h2>	
	
	<p> A first step, post acquisition, was understanding multiple elements of the marketing mix in order to determine how to best restate the brand for accelerated growth. The business team had specific interest in updating brand positionings, packaging/label designs, and flavor profiles (with particular focus on alternative sweeteners). Because speed to market was so critical, the team needed a solution that compressed the cycle time but still provided confidence in the results.

</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>Our proprietary approach blended <a href="https://researchamericainc.com/services/quantitative-data-collection.php">quantitative</a> and <a href="https://researchamericainc.com/services/qualitative-marketing-research.php">qualitative methodologies</a>. This approach allowed the team to confidently understand the relative strengths and weaknesses of each positioning, and
which packaging/label designs best delivered against that positioning.

</p>


<h2> The Outcome</h2>	
	
<p>Unexpected learning surrounding differentiating product benefits identified several potential product lines within the brand umbrella. Brand growth of +18% was realized in the first year post-launch of the repositioned brand.</p>

