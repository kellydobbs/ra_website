 <p> Unless you have recently purchased a faucet, you probably haven't given faucets much thought. Why Should you ? After all, the average homeowner replaces their kitchen faucet every 15 years.  </p>
	 
	 
	<p> Herein lies the challenge for faucet manufacturers;  developing faucets for the mass retail markert requires walking a tightrope between capturing the latest design and being neutral enought to last a couple of decades! While other factors come into play in the decision, the design must draw the buyer in and develop a level of passion for the product.</p>
	
<p> While our client had years of faucet design concept test experience, they decided to use SwipeRight™ to see if they could improve predicition of a successful faucet design.
</p>

	
	
<p> Purchasing a faucet is a very visual decision focused on faucet cosmetics, because consumer expectations of functional features are larely equivalent (you get water when you turn it on).
	Brand and price also come into play play, but design is the dominant decision factor.
	
	
<p>SwipeRight™ was a perfect fit and "bolted on" to the clients existing concept test approach, simulated shelf	
	
	
	
	testing.
<p>The project was completed among recent past purchasers and serious intenders of faucets. Respondents evaluated twelve faucets images, a mix competitive / client benchmarks (actual product images) and new prototype designs (CAD images) in the Swiperight™ section survey.</p>

<p>Comparing the SwipeRight™ results to reliable sales data, we found that
SwipeRight™ accurately predicted sales with an error rate of less than .01% - better than the model developed on metrics captured in traditional concept test section (purchase intent,appeal, etc.) </p>
	
	
	
<p> Interestingly, one highly differentiated faucet design was so polarizing that traditional metrics indicated it should not be launced. However, the strength of passion and positive emotional reaction in the  SwipeRight™ exercise predicted it would be a strong performer. The client launched the faucet, and it has become one the strongest sales performers in the retail
	aisle and is now being copied by competitors.</p>
	
<p> The strong results and predictability, combined with the avoidance of a missed opportunity, made  our client
