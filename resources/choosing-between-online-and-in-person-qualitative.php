	<h1>
Choosing Between Online and In-Person Qualitative: Pros and Cons</h1>
	

<p>Qualitative research seems to be undergoing a bit of a renaissance lately. Qualitative research is the best tool for getting a deep understanding of your consumers’ reasons, opinions, motivations. That can give you insight into the specific research problem, or help you develop hypotheses for additional quantitative research. What’s most appealing about qualitative research is that it allows you to dig deeper and get beyond the surface level responses. </p>
	
<p>One of the key reasons qualitative research is enjoying a renaissance is the availability and proliferation of online qualitative research techniques and platforms. Online qualitative research refers to all forms of qualitative research conducted online rather than face to face or via telephone. In addition to online focus groups and IDIs, it can include diaries, blogs, market research, online communities (MROCs), and ethnography. Some of the more common techniques in use today are:
</p>		
	
	<ul>
		<li> Video in-depth interviews </li>
		<li> Video focus groups</li>
		<li>	Bulletin Boards </li>
		<li>	Online journals</li>
		<li>	Online and text chats</li></ul>
	
<p class="med-link">Pros and Cons of Online and In-person Qualitative</b> </p>
<p>As popular and effective as online qualitative can be, it is not appropriate for all situations. There are some projects where in-person qualitative is the best solution and others that cry out for online qualitative. Here are the pros and cons of each:</p>

	<center><img src="../_img/prosandconschart.png"  alt="Pros And Cons Chart" width="624" height="597"  /></center>
	
	
	
<h2>How to Decide Between Online and In-Person Qualitative</h2>
<p>There are no hard-and-fast rules about choosing one methodology over the other. As always, think about your project objectives and the characteristics and capabilities of different qualitative techniques to find the one that gives you the most flexibility and still meets your project needs. 
In general, you will want to choose online qualitative if:</p>

	
	
	
	
	
	
	
	
	
	
	
	<ol>
      <li>
	  Have a low-incidence, hard-to-recruit population</li>
<l>
	<li>	You want to access participants across multiple geographies</li>
	  
  <li>	You plan to integrate several research approaches and techniques within a single study</li>

   <li>			Want to evaluate in-context usage or consumption behavior and settings as they occur</li>
	
	<li>	You have a very complex topic and/or hierarchical audience (e.g., physicians)</li>
	<li>	You hate to travel</li></ol>

<p><b>On the other hand, you will probably choose in-person qualitative if:</b></p>
	<ol>
	<li>	Need topline results quickly</li>
	<li>	You need to build personal rapport and group interaction</li>
	<li>	You need to observe participants first-hand in a physical setting</li>
	<li>	You can’t resist the glamour of business travel</li>
</ol>
	
	
  <p>  	
Whether you use online or in-person qualitative research (or both!), you will have the benefit of improved insight into your consumers’ behavior and motivations. By taking advantage of the flexibility and creative opportunities presented by qualitative research, you will be able to probe into the “why’s” of your consumers’ decision-making.</p>
