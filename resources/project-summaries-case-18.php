	<br><strong> Project:</strong>  International hotel chain loyalty program </p>
	

	<h2>Background</h2>		
 <p>A major hotel client wished to assess their image across multiple geographical locations (11 countries). Concurrently they wanted to understand attitudes and behaviors related to their ‘loyalty program.’ The rewards program was well developed in some countries, but less firmly established in others. To build awareness and utilization, the customer marketing team created three different prospective print advertising campaigns that could be used. 

 </p>




<h2>Approach</h2>	
	
	<p>RAI executed quantitative online research to identify the best overall creative to move forward to final stages of development and introduction. RAI’s proprietary CLIP ratings were used to evaluate the three campaigns relative to client and industry norms. Additional communication objectives were assessed as well, including how the ads/campaigns drove specific brand imagery and emotions, optimization opportunities, and reactions of core targets such as brand loyalists, competitive loyalists, business vs. leisure travelers, etc.



</p>

	
	<h2>Results</h2>
	
	<p>A clear winning campaign with global appeal and relevance was identified. The brand launched a global rebranding initiative the following year. The new campaign led to significantly increased consideration of the hotel, increased enrollment in the loyalty program, and an increase in number of stays and number of nights.

</p>

	
	
	
