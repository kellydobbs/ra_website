and highly valued for their insight, will transition to senior consulting roles within the company. </p>

<p>Research America President Robert Porter comments: “Intellitrends enjoys a high profile in the 
market and marketing research fields, and we're proud to add Marlene and Keith Stone’s experience
 and knowledge into our company.” He continues: “Their entire team of marketing research experts 
 will bring their passion and dedication into our organization.  We are looking forward blending 
 Research America Inc. and Intellitrends unique marketing research services to the Michigan and 
 broader markets.” </p>

 <p>  Intellitrends’ leadership brings strong expertise in global quantitative and qualitative experience 
 for the consumer and B2B markets, unique observational, experiential and digital techniques for the retail market. </p>Intellitrends has developed products which offer innovations in tracking consumer trends and business competitive dynamics: Marketview 360, In-Touch Now, it Win/Loss and iMobileShare.  

<p>Research America continues to expand its network of local marketing research centers by evaluating
 opportunities to open local, branded offices throughout the United States. The firm has established 
 marketing research facilities based from its headquarters in Newtown Square, Pennsylvania. The company
  has expanded into pharmaceutical, consumer goods, food and healthcare marketing research. It offers energy
   and utility companies dedicated marketing research specialty expertise through Metrix Matrix, located in
    Rochester New York. The company owns a central testing location facility in Totowa, New Jersey. </p>
 
    
    
    


