<br>
	<p>Research America’s marketing research analysts use <strong>perceptual mapping</strong> to understand how your products are perceived by different segments your target audiences. Perceptual mapping brings insight into position and market your current product, how to develop your messaging strategy, and can help bring ideas into developing new products.  
	<p>In perceptual mapping, interviewers ask consumers qualitative research questions about their experience with a product, focusing on variables like perceptions of performance, packaging, price, size, etc. Their answers are plotted or traced on an X/Y axis chart, known as a perceptual map, using a scale (such as a Likert Scale). </p>

	<p>Perceptual mapping helps marketers and brand managers understand how their target consumers perceive their options in a highly competitive market. <strong>Perceptual maps</strong> are good at showing correlations between individual products and the attributes that set them apart from similar competitive products. </p>

	<p>Perceptual maps are as simple or complex as the number of attributes your study wants to correlate to products for sale. To build a simple perceptual map, researchers design questions which ask respondents to use a numerical scale to rate how much they agree that the product under evaluation has the particular attribute being tested.</p>

	<p>When perceptual maps become more complex, researchers are evaluating correlations with multiple different attributes. These attributes are evaluated for their overall influence on how they influence decision making and ordering of the product’s other attributes. </p>

	<p>Research America’s statisticians and analysts build perceptual maps to assist marketing and brand managers in discovering which brands align most closely with the attributes that drive their target audience’s decision-making. Our decades of experience using statistical tools to support marketers and brand managers in virtually every industry will help your business grow its product line and reach more consumers.</p>
