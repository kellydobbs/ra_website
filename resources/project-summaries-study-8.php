	<p> <strong> Industry: </strong> Pharmaceutical	 &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; 
		<strong>Methodology:</strong> In-person focus groups<br>
	<strong> Project:</strong>		  
	  Children’s medicine – patient experience / positioning </p>
	
	

	<h2>Background</h2>		
 <p>A major drug manufacturer wished to understand treatment dynamics among parents of children with severe allergies. Previous research uncovered that patients at risk weren’t being appropriately assessed and protected. Further insight and understanding of patient treatment options in the severe allergy space was needed, like: </p>
	 <ul check>
	<li>	Understanding of patient/caregiver experiences/beliefs, habits/practices</li>
	<li>	Exploration of the perceived barriers/benefits to available treatments</li>
<li>Assessment of four potential new positioning platforms</li>

</ul>


<h2>Approach</h2>	
	
	<p>  Participants provided quantitative information about their experiences with severe allergies, including symptoms experienced, frequency, treatment behaviors, products used, sources used for information/education. Positioning statements were quantitatively evaluated on key measures such as relevance, benefit offered, likelihood to take action. Qualitative discussion gave depth of understanding around their emotion responses to experiences with severe allergies and treatments as well as explained responses to the positioning statements.

</p>

	
	<h2>Results</h2>
	
	<p>The research illustrated sufferers’ experiences and the emotional/physical toll severe allergies can have. It also defined barriers to treatments and possible ways to overcome those barriers. Lastly, it identified the most compelling positioning statement as well as optimization opportunities to make the statement even stronger. Client launched an education and awareness campaign, including DTC TV and print advertising, based on the most compelling message identified in the research. Unit volume and sales have increased significantly post-campaign.</p>

