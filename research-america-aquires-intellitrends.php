	<h1>Research America Expands with Michigan-based Intellitrends</h1>    
<p>Research America has expanded its national presence with the addition of Intellitrends to its 
corporate family of market research companies.  Intellitrends, a strategic marketing research firm
 based in Clarkston, Michigan was founded by Marlene and Keith Stone in 1989.  Over the past 27 years
  Intellitrends has worked closely with major clients in all industry sectors helping them achieve
   market growth through strategically-focused, quality-driven and factually based market insights. </p>

<p>Intellitrends will continue to operate under its own trade name as a wholly owned subsidiary 
