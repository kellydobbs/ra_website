<p>	<span class="big-link">  Study #13 </span> </p>	
	
	<p> <strong> Industry: </strong> Apparel
		&nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; <strong>Methodology:</strong> Multi-phase, Multi-mode, online/in-person <br>
	
	<strong> Project:</strong>		  
	 Footwear – Assessment of Market Opportunities</p>
	
	
	<h2>Background</h2>		
 <p>Constantly evolving fashion trends, retail formats, and cultural lifestyles drove shifts in wardrobe usage and brand consideration sets. The client brand has high awareness as an authentic, quality footwear brand. Consumers generally had low awareness of points of differentiation. Objectives were to:
</p>
<ul check>
	<li>	Understand apparel consumer segments</li>
	<li> Learn how to create and maintain deep emotional connections </li>
	<li>	Learn key behavior/attitude drivers</li>
	<li>	Shape communication strategies regarding relevant brand messages and product benefits by segment</li>

	</ul>

<h2>Approach</h2>	
	
	<ul>
	   <h3>A multi-phased segmentation and equity study across 5 countries, males and females: </h3>
	   <li>	Phase 1 – Internet-based broad segmentation research on lifestyle, beliefs, leisure activities, media habits and fashion attitudes.</li>
	   <li>Phase 2 – Internet-based equity research among same consumers to overlay brand perceptions to identify segments.</li>
	   <li>	Phase 3 – In-person blended research to validate segmentation model, identify relevance of brand, product, and messaging.</li>
	   <li>	Phase 4 – Continual in-market activity tracking and algorithm development allowed segments to be refined as they evolved.</li>
	    
	    
	    
      
    </ul>
	<h2>Results</h2>
	
	<p>The research defined and monitored a consumer segmentation model that served as the basis for marketing, product design and go-to-market strategies. It provided clear articulation of the brand’s uniquely owned brand essence and DNA with concise brand characteristics and design principles. It also enhanced overall marketing effectiveness by defining the role and purpose of each element in marketing mix by core segment. Finally, it drove seasonal marketing process to align with and create business value for the product development and go-to-market calendars.</p>
	
	
	
	
