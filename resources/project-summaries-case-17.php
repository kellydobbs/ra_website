	<p> <strong> Industry: </strong> Small-town municipality &nbsp; &nbsp; &nbsp;  &nbsp; <strong>Methodology:</strong> Survey <br>
	
<strong> Project:</strong> Community Survey Among Residents and City Employees </p>
	
	<h2>Background</h2>		
 <p>The City Council of a port-side community was seeking to identify the level of satisfaction of its citizens and city employees. It sought to understand the current attitudes and opinions of residents in order to create strategic plans for the future, specifically related to areas of budgetary spending and development. Understanding the top priorities of residents in the community would help the council set the proper strategic course. Budget allocation, improvement services, and the overall quality of life are important considerations for any city or municipality. 

 </p>




<h2>Approach</h2>	
	
	<p>A sample of the employees that worked in the city was provided to RAI with a high degree of confidence along with a sample of random residences within the city. The city manager, city council, and their team were very clear on areas of importance to explore, which the survey instrument presented.



</p>

	
	<h2>Results</h2>
	
	<p>Residents likely feel as if they have a higher quality of life than similar markets. Two important key areas are the development of the harbor district and an expansion of Internet services. Findings showed that residents felt there was a need to improve road maintenance, but felt the city was showing improvement in this area. The City Council was able to develop strategic plans which appealed to residents. They discovered a strong correlation between a high quality of life and satisfaction of its employees. This led to improved programs to help increase satisfaction levels.

</p>

	
	
