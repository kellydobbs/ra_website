	<strong> Project:</strong>		  
	Heartburn medicine – ad testing</p>
	
	
	<h2>Background</h2>		
 <p>A manufacturer was set to introduce a new product (PPI) and had developed several ads designed to ‘bring the product to life’. Objectives were to:


</p>
<ul check>
	<li> 	Evaluate 5 advertising spots on ad performance measures n</li>
	<li>	Determine if any of the designs created greater breakthrough than the Current offering </li>
	<li>	Understand drivers of appeal, relevance, believability and fit with the brand</li>
	<li>	Understand the thoughts, feelings and emotions that each animatic elicits</li>
<li>	Understand specific optimization opportunities for each animatic</li>

</ul>

<h2>Approach</h2>	
	

    <ol>
      	<li>	CLiP communications testing vehicle was utilized to address the objectives.</li>
	<li>	In this instance a ‘blended’, quantitative-qualitative CLT was deployed. </li>
	<li>	8 sessions of ~20 consumers were recruited. 
	<li>	During the first hour of each session consumers completed a quantitative CLiP evaluation of the 5 animatic spots. </li>
	<li>	In the second hour, a focus group was competed with 6-8 respondents from the session.</li> 

    </ol>

	
<h2>Results</h2>	
	<p>Although all five ads created interest in the product, two of them were most successful at capturing attention, differentiating the product, and communicating ‘life without heartburn’. </p>

	
	
