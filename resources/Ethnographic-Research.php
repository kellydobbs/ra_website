<h2>Ethnography in Action</h2>
	<p><u>Increasing Weekend Get-Away Visitors</u><br>
	
   A major city in the Southeastern U.S. wanted to increase weekend-travel by visitors from nearby states. While the client thought they had a lot to offer visitors, they wondered how different groups of potential visitors made their decisions about weekend get-aways. Participants were recruited from neighboring states, focusing on residents of cities within a 3-hour car trip. Families and young couples were the focus of this research. Participants were all planning a weekend get-away, and interviewers observed how they decided where to go on this get-away, including how they completed the research about accommodations and activities, who made the final decision, and how the plan was implemented. If the city was not their get-away destination, participants were offered an attractive incentive to choose the city. Interviewers then observed the participants throughout their visit to the city. The research identified several critical pain points that were addressed through additional information and marketing communications, with the result of increasing neighboring state visitor traffic by 15%. </p>

	
	<p><u>Housecleaning for People with Allergies</u><br>
	Another example of ethnographic research , as reported in Marketing Week, involved German household appliance manufacturer, Miele. Specifically, Miele wanted to understand how people clean their homes if they have a family member with severe allergies. Miele, through its ethnographic market research, realized that these households were cleaning non-stop, since they were not sure when surfaces were completely clean. This led Miele to design a vacuum cleaner with a traffic light indicator that shows when a surface is dust-free. Additionally, Miele also observed how careful people with allergies were with their laundry and so introduced a washing machine with a program for washing pillows and a rinse process that removes detergent residues. Olaf Dietrich, a director at Miele, explained the research, “… We realize that it is essential for not only marketing but also engineers to see the issues first-hand. Only if you are present do you really understand the issues.”
	</p>
	
	
<h2 class="bold">Summary</h2>	
	Ethnographic research can be a powerful tool to uncover new and unique insights about your customers, their behaviors, and where the market may be moving. While this research is still qualitative, results can be confirmed with traditional qualitative research. The key to ethnography is to use trained and professional interviewers to make sure that the information gathered is not biased and is interpreted properly.

	
