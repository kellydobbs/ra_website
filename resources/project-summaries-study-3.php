	<h2>Background</h2>		
 <p>The client wanted to identify current opportunities and reveal anticipated challenges as they increased their presence in the community. Community concerns over a negative ripple effect have discouraged the plans of other organizations to expand. The client wanted to mitigate that risk to their expansion plans. As the client approached the projected ground breaking on their new medical facility, the opportunity existed to optimize community perceptions, facilitating a true integration with the community and surrounding area and reinforce the commitment and trust of the brand. 

</p>




<h2>Approach</h2>	
	
	<p>The client wanted to conduct a poll of the area residents to capture their feelings regarding a new medical facility in the area. Telephone surveys were administered to 375 area residents to measure the following issues: </p>
<ol>
		<li> Current perceptions of health care options in the community</li>
	<li>	Awareness and use of the healthcare brand</li>
	<li>	How they are aware of the brand name</li>
	<li>	Perceptions of building a new facility in the community</li>
	<li>	Desired health care services and anticipated use </li>
</ol>
	
	
	
	
	<h2>Results</h2>
	
	<p>The client broke ground on their new facility. With incredibly high awareness levels, they can now position the new facility by listening to the public, capitalizing on community values, and highlighting the ‘full service’ and ‘accessible’ features. Use of local media outlets is a critical source of information for the community, so desired healthcare services, primarily a new Emergency/Trauma Center, should be promoted though local advertising media. The client will also need to acknowledge and appreciate the public’s primary dissuading concern for traffic problems and communicate solutions.</p>

