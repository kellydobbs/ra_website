<p>Research America conducts your study through a blend of approaches. Our consultants will introduce each methodology and technique to you, and explain the benefits and weaknesses of each approach. We will work together to design a step by step study design for your unique project. </p>
  
<h3>Step 1: Quotas and Sampling</h3>
 <p> When your research includes quantitative research in the form of a survey, we start by determining acceptable levels of statistical tolerance (minimum 95% +/- 4.4%) for results obtained through questionnaires. We calculate the quotas required for surveys and other forms of research, including focus groups and depth interviews.  We choose the best sampling methods for the different components of your study, and obtain access to potential respondents that meet your exact criteria. Your samples may be drawn from your client lists or from other resources Research America can access for your study. Using this information, we finalize a sampling plan 
    based on estimated response rates. </p>
 
 <h3> Step 2: Study Design</h3>
 <p> Once we’ve established which questions need to be asked and who will be interviewed, we design your survey instrument, scripting, and discussion guides. We categorize the attributes you need to measure and write
  questions around your exact objectives and the best practices in your industry. We also create a set of specific Quality Assurance procedures for your individual study.</p>
  
  <h3>Step 3: Data and Reporting Decision Making</h3>
 <p> Research America’s robust reporting systems deliver data in many different formats, ranging from simple banner tables to full-color graphical representations of your findings. 
 Our experts are available to interpret your data to answer questions about your study. Our team members can create a customized report for your study and are available for on-site or remote
   presentations. Our presenters are highly regarded by their industry peers, and will offer an engaging overview of your study to your organization and stakeholders. </p>
    
 <h3> Step 4: Programming &amp; Interviewer Training</h3>
 <p> When your study involves a survey, programming your approved survey instrument into our telephone interviewing system or internet-based system is your next step in the Survey and Study Design process.
  When self-administered paper-based surveys are part of your activity, we can format and print to your project’s marketing specifications. We also print any discussion or interview guides used by our depth -interviewers and 
   focus group moderators during these areas of your project. </p>
   
 <p> Once survey instruments, scripts and contact lists are approved, we test all systems involved in your research. We select and train interviewing staff and field test the various components of the survey,
  adjusting where necessary. </p>
  
  <h3>Step 5: Fielding, Analysis and Data Delivery</h3>
 <p> Once your project goes live, you can interact with our online dashboards to monitor the progress of your study, and your project manager will be available to update you on progress and any issues that arise in
    the field. Listening, viewing and other monitoring activities are available depending on the techniques and methodologies involved in your study. You may monitor the progress of your interviews through contacting
      your project manager.</p>
      
