	<h2>Background</h2>		
 <p>The purpose of this study was to obtain consumer feedback and attitudes relative to proposed shelving prototypes to measure interest in the new shelving designs, identify and prioritize preferred concepts or features, and explore and identify market behaviors and dynamics that impact preference for and motivate purchase decisions relative to shelving. The specific product concepts the client wanted to receive feedback on were decorative shelving, decorative shelving brackets, hooks, and free-standing shelving units. </p>




<h2>Approach</h2>	
	
	<p> For each category of product concepts, a national survey of random sampling was conducted. Most popular decorating preferences, such as primary home decorating style, wood color/finish, and hardware color/finish, were identified. Also identified were the stores respondents frequent, what retailer they would expect to find the products, who tends to make the purchase decision and who does the installation. Each of the individual product concepts were tested in the same manner â€“ by asking questions about a photo of each concept. 


</p>

	
	<h2>Results</h2>
	
	<p>With so many ideas on the drawing board it is difficult to make internal decisions as to the desire of the consumer which product concepts will be successful in the marketplace. The expense of design, resources, manufacturing, distribution, and advertising requires us to research the market first to get a strong degree of confidence our products will be successful. Conducting comprehensive analysis during the design stage allows us to get the right product to the market sooner, thus generating a quicker revenue stream and avoid costly mistakes. </p>

	
	
	
