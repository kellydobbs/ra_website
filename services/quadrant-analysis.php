	<h1>Quadrant Analysis</h1>	

<img src="../_img/services-2.jpg"  class="focus-group-facilities-11" alt="quadrant analysis in market research"> 
		
	
	<p>In <strong>Quadrant Analysis</strong>, Research America’s experts take consumer opinion about various features belonging to your product, and contrast them with your competitors’ product features. Statisticians plot the joint position of these two factors in a two-dimensional quadrant graph so that you can immediately see which important features are most liked and perform well, and where your product’s features don’t perform as well. Using <strong>Quadrant Analysis</strong> also reveals which less important features that rate highly vs. your competition, and where every company ranks poorly in consumer opinion. </p>

<p>Research America uses <strong>Quadrant Analysis</strong> in the analysis of <strong>market research surveys</strong>, customer surveys, and employee surveys to investigate gaps between your expectations and actual performance. Quadrant Analysis lets you know exactly where you stand in the market, and reveals areas that you can take advantage of through development. </p>

	<h2>How Quadrant Analysis Works</h2>

	<h2 class="bold">Research America consultants design studies that ask participants to rate the importance of several different categories, including: </h2>
<ul>

	<li>Availability</li>
<li>Ease of use</li>
<li>Feature richness</li>
<li>Look and feel</li>
<li>Price</li>
<li>Environmental sensitivity</li>
<li>Quality</li>
	<li>Location</li>
	</ul>


	<p>While the categories may vary based on what is evaluated in any particular study, the process remains the same across all types of research. Analysts take the data and use software to plot the results in a chart that measures the rated importance with the actual brand performance. </p>
	
	
