<h1>Substantiation Basics</h1>

	<p class="med-link">Ad Claims 101 - Substantiation Basics </p>
	

	<p>		As a marketer, you may have yet to face having a claim in your advertising and marketing challenged by your competitor or consumer. But those marketers who have experienced the need to produce claims substantiation proof post hoc know that it is far, far better to think about how you can prove it before you make a claim rather than after you are called out to produce your substantiation. 
</p> 


	<p>First, keep in mind that there are two types of claims: explicit and implied. An <strong>explicit (or expressed)</strong> claim is made when you say the claim directly; an implied claim can exist when the claim isn't said directly, but a "reasonable person" could assume it based on other statements you have made, or images you have used. But there's the trap: Who is a "reasonable person"? Think hard and carefully about what implied claims might exist to consumers. </p>
	
	
	
	<p class="med-link">Additionally, there are many different types of claims advertisers use:</p>
	
	
	
	
	

<ul>
  <li> 
	  The most widely used – and perhaps the most valuable - in marketing, comparative claims make a direct "call out" to a competitor. These claims include claims of superiority over the competition ("My brand is better than the other guy's."), claims of parity with the competition ("My brand is just as good as the other guy's."), and claims of superiority over yourself (Now my brand is better at doing something than it was previously.) Because of this direct approach, these claims are most likely to be challenged and to require substantiation.</li>

    
   
  <li> 		<strong>Non-comparative claims </strong>are usually about product performance and do not make a comparison to either competitive or previous products. However, because your claim could be considered subjective sensorial properties, you will make sure you have consumer substantiation to support this claim. </li> 

  <li> 		<strong>Market performance claims </strong>are typically not proven by consumer research; rather, they are proven by industry measurement or syndicated research. For example, if a company claims to have the top-selling product, industry metrics can demonstrate whether or not this is true. </li>
	
<li>		Another type of claim not proven by consumer research includes scientific and technical claims, which are, of course, substantiated through scientific or technical analysis. And keep in mind, even if a claim is technically/scientifically proven, marketers must also consider whether a "reasonable consumer" could be confused by the message.
</li>	
	
	
</ul>
	
	<p class="med-link">Does My Claim Require Substantiation?</p>
    
 <p>Thankfully, not all claims require substantiation. For instance, "puffery," which includes "exaggerated blustering," commonly accepted truths, and "many people" statements are claims that do not require substantiation. Puffery is acknowledged to be obvious hyperbole and not objectively measurable.</p>
	
	
