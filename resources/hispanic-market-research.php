<h1>Hispanic Market Research: How It’s Changed in the Past 15 Years</h1>

<p>America is changing. If 2020 wasn’t a clear indication of the forces reshaping our nation, then consider some of the following statistics compiled by <a href="https://www.pewresearch.org/fact-tank/2017/09/18/how-the-u-s-hispanic-population-is-changing/" target="_blank" ><span>Pew Research Center</span></a>, PBS and more:</p>
<ul>
<li>In just the past 15 years, the U.S. Hispanic population has increased from 42 million to just over 60 million, and it is projected that by 2050, that number will reach 128 million!</li>
<li>Approximately <a href="https://www.pbs.org/newshour/nation/3-ways-that-the-u-s-population-will-change-over-the-next-decade" target="_blank"><span>19%</span></a> of the total U.S. population consider themselves Hispanic. While the Hispanic population’s rate of growth has been slowing since 2005, experts project that Latinos will account for 29% of the U.S. population by 2050 (up from 14% in 2005).</li>
<li>Hispanics are among the <a href="https://www.pewresearch.org/fact-tank/2018/09/13/key-facts-about-young-latinos/" target="_blank"><span>youngest ethnic groups</span></a> in the U.S. with a median age of 30 years in 2019.</li>
<li>Today, the majority of Hispanics living in the U.S. are born in America: <a href="https://www.pewresearch.org/fact-tank/2018/09/13/key-facts-about-young-latinos/" target="_blank"><span>81%</span></a> of those 35 years and younger were born here in the U.S. rather than abroad.</li>
</ul>
