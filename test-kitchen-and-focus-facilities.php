 	<h1>Research America Test Kitchen and Focus Facilities Get a Refresh
	</h1>
 	<h3>New Jersey, April 4, 2016</h3>


	<p><img src="_img/FG Viewing Room1.jpg" class="focus-group-facilities-1"></p>

<p>Research America, Inc., a full-service marketing research firm, has recently updated its Central Testing Location’s test kitchen in Totowa, New Jersey. Kitchen appliances are replaced frequently to match consumer buying trends and to ensure that the appliances hold temperatures accurately and produce consistent results across every batch of product prepared. <br>
	<br>

 Company procurement specialists searched for a consumer model range oven unit suitable for supermarket product testing and other home cooking applications.   Buyers finally selected a recent-model Samsung-brand range oven designed for the average home cook, which also met specifications used in commercial food service environments. 
</p>	
	
	<p></p>
	
		<p><img src="_img/Kitchen 2.jpg" class="focus-group-facilities-1"></p>

<p>Our main goal in the test kitchen is to simulate how homemakers prepare their day to day meals in the home”, commented Jorge Santiago, Totowa Project Manager. “We ideally want to use consumer brands, but we’re looking for appliances with similar specs as the commercial equipment, in particular, featuring more durable surfaces and a wider range of cooking temperatures for the burners as well as the ovens. We do this so we can also service clients testing food-service products.
</p>

<p>TLC activities rely heavily on test kitchen appliances, which are used in nearly 70% of projects performed for clients each year. Ovens and burners fire up in continuous hourly
 use approximately 17 work days in every month.
 </p>
<p><img src="_img/Kitchen 1.jpg" class="focus-group-facilities-1"></p>

<p>Research America’s Central Testing Location (“CTL”) facilities in New Jersey are conveniently located 20 miles from downtown Manhattan and from Newark Liberty International Airport. In addition to Food and Beverage testing, the facility is available for fragrance, cosmetic and other consumer opinion research.</p>
<p>Beyond CTL capabilities, the center offers focus group moderation and depth interviewing facilities on-site. In addition to its Totowa facilities, Research America’s provides a network of full-service marketing research offices currently based in Newtown Square and Philadelphia, PA; with branches in Lehighton and Pottsville, PA, Rochester, NY, Chicago, IL and Sacramento, CA.
</p>





	
