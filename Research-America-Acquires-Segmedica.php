 	<h1>Research America Acquires Segmedica (xsperient®)</h1>

	

<p> <strong>Newtown Square, PA, January 4, 2021 - </strong> Research America, Inc., the top marketing research firm located outside Philadelphia, announced today that it has acquired Segmedica (xsperient®).  This expansion brings Research America’s cumulative number of acquisitions over the past six years to 16. Each of these acquisitions has created new capabilities for Research America, providing its clients with unique, all-inclusive services completed by one united team.  </p>
<p>Segmedica is a prominent market research company in the field of applying the behavioral sciences to healthcare and wellness consulting, market research and market segmentation. Located in Buffalo, NY, Segmedica has conducted hundreds of studies throughout the US and around the world.</p>

<p> <i>“We are excited to welcome Peter and Donna Simpson of Segmedica to our family of leading market research companies,”</i> said Robert Porter, CEO of Research America, Inc.  <i>“Their collaborative culture and entrepreneurial spirit are a perfect match with Research America’s, and their expertise in healthcare and wellness market research will synergize well with our existing research capabilities in those areas.”</i> </p>

<p> <i>“Merging with Research America will absolutely extend and strengthen our areas of expertise,”</i> said Peter Simpson, Principal at Segmedica. <i>“We are looking forward to this exciting new venture,”</i> added Donna Simpson, Principal at Segmedica.</p>

<p>Peter and Donna Simpson and their entire team will remain in place and will continue to serve their long-term clients in the Pharmaceutical/Biotech, Medical Device, Nutrition, and other Health and Wellness industries such as Food and Beverages, Retail, and much more. With decades of experience in healthcare research, their unique and innovative methodologies provide deep, insightful research that go beyond traditional “emotional research”. Segmedica’s areas of expertise include the following:</p>
<ul class="check">
    <li>Disease areas such as Auto-immune, Dermatology, Pain Management, Oncology, Cardiology, Sexual Dysfunction, Gastroenterology, Ophthalmology, and more</li>
    
    <li>Medical Device areas such as Cardiac, Anesthesiology, Diabetes, Pulmonary, Surgical Devices, and many more</li>
    
    <li>Consumer Nutrition and Nutraceuticals such as Supplements, Nutritional Drinks, and more</li>
    
    <li>Consumer Experience in areas such as Disease Management, General Health and Wellness, Retail Food and Beverage areas as they relate to Health and Wellness, and more</li>
    
    <li>Health Services such as Integrated Delivery Systems, Health Insurance, ACOs, Hospitals, Physician practices, Medical Distributors and much more</li>
</ul>
<p>Research America has grown organically and through acquisitions of select research firms in specialized verticals.  In 2019, the company was named one of the Top 50 market research firms in the United States based on annual revenue by the GreenBook Research Industry Trends Report (GRIT). Research America has also been named by Inc. magazine as one of the fastest-growing privately held companies in America. For more information on Research America’s full-service market research capabilities, please call <a href="tel:610-356-1800">610-356-1800</a> or visit <a href="https://researchamericainc.com/">www.ResearchAmericaInc.com</a>.</p>
