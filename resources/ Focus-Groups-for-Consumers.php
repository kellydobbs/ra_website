<h1>How to Use Focus Groups for Consumer Insights</h1>
<img src="/_img/Focus-Groups-for- Consumer- Insights.jpeg" alt="How to Use Focus Groups for Consumer Insights"  width="746" height="408" class="focus-group-facilities-1"> <p>How to Use Focus Groups for Consumer Insights
As you know, marketing research comes in two flavors: qualitative and quantitative. At the risk of oversimplifying, quantitative research is best used when you need to understand “how many?” and qualitative research is used when you need to understand “why?” Focus groups are a mainstay of qualitative research methodologies.
<br><br>
	
	<h2>What is a Focus Group?</h2>
	According to the Insights Association, “Focus groups are a marketing research technique for qualitative data that involves a small group of people (6-10) that share a common set of characteristics (demographics, attitudes, product usage, etc.) and participate in a discussion of predetermined topics led by a moderator.” Usually, participants receive a stipend, honorarium, or incentive to encourage their participation and thank them for their time. Other expenses (e.g., transportation, parking, etc.) may also be covered, depending on the population and the particular focus group setting. <br>
	Focus groups are most often held in specially designed facilities, with professional staff who can recruit qualified respondents to the group, host the group and client observers, and record the group’s audio and video. Occasionally, focus groups are held in other settings, such as a hotel meeting room or large private restaurant. Any large space with sufficient privacy and quiet will do. However, even in those situations, researchers will typically contract with a professional focus group firm to provide the recruiting, hosting, recording, and so forth that will facilitate the group discussion. <br>
	To ensure that one group is not an outlier, most researchers will choose to conduct at least two groups per location, or per demographic group. For example, if you are including two different sets of consumers in the project, such as purchasers and non-purchasers, you would want to complete two groups for each, or four groups in total. <br>
	All focus groups should be led by a trained and experienced moderator. The moderator will lead the group through the topics included in the moderator’s guide (an outline of the discussion topics or the questions to be covered during the focus group discussion). Most importantly, the moderator sets the tone for the discussion, making participants feel safe and comfortable and free to speak without being judged. Further, the moderator controls and guides the group to ensure that all participants are engaged and heard, and that the group is not dominated by only a few participants.<br>
	Stimuli can also be presented to participants during a focus group. This might be showing advertising or product concepts, taste and product tests, showing brand logos, and just about anything else that marketers need to share to gain important consumer insights. (Here is a project summary about focus groups Research America conducted for a pharmaceutical company to test a children’s medicine.)<br>
	
<h2>Benefits of Focus Groups </h2>

	
	
	<p>Focus Groups are especially useful for those research topics that require deeply exploring the reasons and motivations for consumer behavior, the emotional foundation for decision making, reactions to the customer experience, and other topics that are less rational. (For example, here is a project summary about a focus group project Research America completed to understand living with children with severe allergies.) Focus groups allow researchers to fully explore consumers’ attitudes, even to the point of being able to explore topic not on the moderator’s guide if they are brought up in the discussion. </p>
	<h2>Other benefits of focus groups include: </h2>
	
	<ul class="check">
		<li>			Focus Groups can be used to explore topics to include in follow-up quantitative research. Conversely, they can be used after the fact to help researchers better understand and explain results from quantitative research.
 </li>
		
		<li>			Focus Groups are iterative, in the sense that each prior group may yield some insight that could change the discussion of subsequent groups, thus increasing the learning gained from the overall project.

	</li>
		
		
	  <li>				Focus groups can be completed with B2B audiences as well as B2C audiences, although businesspeople may be difficult to schedule into in-person focus groups or may be reticent to discuss their business in front of potential competitors.
</li>
	  </ul>

	<p>Focus Groups are an excellent and time-tested marketing research tool, but they do have some drawbacks. The main one is geography. Because focus groups are usually conducted in-person, it is difficult to get a wide geographic representation, if that is important to your project. (Note: this can be mitigated with online focus groups.)
	</p>
	
	<br>
	
	<h2 class="bold">Summary</h2>	
	Focus groups are an excellent technique for marketing researchers. While they sound fairly simple and self-obvious, they require training, expertise, and experience to pull off successfully. Misunderstanding what you hear in a focus group, or hearing it from your own biased perspective, can result in serious damage or misdirection to brands.

	
