	<h1>(CAWI) Computer Assisted Web Interviewing</h1>
	
	
<p>Research America offers invitation-based, online surveys <strong>Computer Assisted Web Interviewing</strong> (CAWI) as an option in data collection. The Computer Assisted Web Interviewing studies that we develop are closed to the general population – email invitations containing unique codes and identifiers protect respondent anonymity and keep the survey contents away from prying eyes. Computer Assisted Web Interviewing surveys deliver reliable data from the exact audience you want to interview. </p>


<p><img src="../_img/web-interviewing.jpg"  class="service-img-2" alt="Computer Assisted Web Interviewing (CAWI)"></p>

	

	<p>Research America’s detailed online survey system is able to customize the flow of the questionnaire based on the answers provided to each question. Using Computer Assisted Web Interviewing, clients are able to include different types of multimedia content into their surveys : programmers can present images, audio and video clips, and information published on the Web for evaluation by each survey respondent. While it’s easy to integrate online content into surveys, there are many factors involved in designing an online questionnaire that necessarily limit the origin and type of the media content clients will want to include: bias, available question formats, administration,
     quality and ethic issues need to be reviewed carefully. </p>
	
	<p>Many of our clients rely on Computer Assisted Web Interviewing because it’s budget-sensitive: online questionnaires don’t require live interviewers, research devices and don’t involve costs in replying on the respondent’s part. The most important aspect of online surveys is questionnaire design, because the quality of the data collected via Computer Assisted Web Interviewing is only as good as the quality of the questionnaire itself, </p>

	<p class="med-link">Main Advantages</p>
	
	<p>In addition to budget-friendliness, <strong>Computer Assisted Web Interviewing</strong> advantages include: </p>
     
          <p class="med-link">  Tailored Design </p>
	<p> Well-designed online surveys integrate skip-logic – the pathways through each survey are determined by the respondent’s individual answers to previous questions.</p>
	
	
	<p class="med-link">Clarity </p>
	<p>Programmers can structure the questionnaire for ease of compilation – control check, drop down menu, sub-menu and autocomplete writing help facilitate compilation.</p>

 
	<p class="med-link">Online help </p>
 <p>Programmers can insert pop-up help boxes to assist each respondent to complete each questionnaire, substituting for any neutral/ impartial help that a live interviewer would provide to clarify a question.
	</p>
 
 
	<p class="med-link">Lower costs</p>
 
	<p>No printing costs, paper costs, or return postage as with mail surveys; no tablet devices to purchase; no live interviewer costs.</p>

 	<p class="med-link">Reduced time</p>

	<p>Survey data is collected for analysis immediately – results are sent in real time to the main database, and fed directly into statistics software applications. In many instances, real-time analysis and statistics are available for view via dashboard connection.</p>


	<p class="med-link">Drawbacks</p>
	
	<p>Computer Assisted Web Interviewing ease of implementation and fast-tracking of results are impressive, but there are some issues to consider in choosing Computer Assisted Web Interviewing over computer assisted telephone interviews(CATI) using a live interviewer.  A live interviewer speaking directly with the respondent can provide help and neutral, impartial guidance to complete the questionnaire if a question isn’t easily understood – the lack of live help may be considered a drawback of Computer Assisted Web Interviewing. An important issue to consider when choosing CAWI is that not every member of your potential survey sample may have a reliable, personal internet connection. Limiting your respondents to only those who can respond online may limit your audience -- your target would be wide, but not universal. Another issue when considering CAWI is maintaining the attention of the respondent—lengthy online surveys risk respondent abandonment, leaving the survey incomplete, Alternatively, a survey that fails to keep the attention of the respondent may result in low-quality responses and skewed data as a result.</p>


