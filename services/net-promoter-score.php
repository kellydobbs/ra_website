	<h1>Net Promoter Score</h1>

	
	<img src="../_img/net-promoter-score.png" alt="Net Promoter Score" class="net-promoter-score">

	<p>Research America integrates <strong>Net Promoter Score</strong> (most often referred to as NPS®) into our research measuring customer loyalty by gauging how willing a customer is to recommend a product or service. Developed by Bain & Company’s founder of Loyalty Practice to measure customer loyalty, marketing researchers across the field have recognized its value and simplicity, and its ability to be applied to virtually every industry, by organizations of any size, anywhere in the world. </p>

	<p>The <strong>Net Promoter Score metric</strong> asks, “How likely is it that you would recommend an organization/ product/ service to a friend or colleague?” </p>
	<p> Your respondent’s answer helps Research America’s analysts in:</p>

	<ul>
		<li>Segmenting customers by loyalty</li>
		<li>Identifying unsatisfied and at-risk customers</li>
		<li>Helping clients optimize around a single customer metric</li>
		<li>Benchmarking against industry and competitor Net Promoter Score </li>
		<li>Discover customer loyalty drivers</li>
	</ul>

	<p>Research America believes that <strong>Net Promoter Score</strong> is a valuable metric that helps customers grow revenue and profits. It’s simple to implement in quantitative research surveys, and collected score data flags actions that clients should address quickly, especially in closing the loop with customers who are not promoters, by working to improve scores in any area (customer service, product, brand image, etc.).</p>
	
	<p>How Net Promoter Score® Works</p> 
	
	<p><strong>Net Promoter Score</strong> questions are asked after a telephone or website transaction takes place, or as part of an ongoing customer service study. Our team builds a system to intercept your customers, asking them to answer the NPS-type question along a scale that ranges from 0 (not at all likely) to 10 (extremely likely). Our analysts use <strong>Net Promoter Score</strong> to place your customers into one of three groups based on their response – your customers are either Promoters, Passives and Detractors.</p>
	
	<p>Your customers will fall into one of the three groups based on their answer:</p> 
	<p class="left">Detractors = 0 – 6<br>
	Passives = 7– 8 <br>
	Promoters = 9 –10</p>
	
	<p>Promoters are your best, loyal and most enthusiastic purchasers and supporters of your brand. Your fans and superfans will continue to purchase your products and services, and most importantly, share their good experiences with others. These referrals are viral: good word of mouth brings in new customers at a lower acquisition cost.</p>
	
	<p>Passives are satisfied customers, but they aren’t as enthusiastic as the promoter group, rating your organization at 7 or 8 on the Net Promoter Score scale. Many marketing experts believe that your neutral, passive customers are very receptive to your competitions’ offerings and may defect if your competitor’s price and/or shopping experience match their need.</p>
	
	<p>Detractors have rated your organization between 0 and 6 on the NPS scale, expressing their unhappiness with your brand. Marketers believe that even the most neutral 5 or 6 rating customers have the potential to spread negative opinons about your brand, and recommend outreach to this group to correct issues and take action to raise NPS scores, making Detractors into Promoters in the process.</p>
	
	<p>The Net Promoter Score® Formula</p>
	
	<p>After Research America’s analysts classify your customers into these three groups, we calculate your score by subtracting the percentage of Detractors from the percentage of Promoters.</p>
	
	<p class="left">NPS = % Promoters – % Detractors<br>
	or<br>
	NPS = (# Promoters / Total Respondents) – (# Detractors / Total Respondents)</p>
	
	<p>We drop the percentage sign, and represent the score as a whole number. Your <strong>Net Promoter Score</strong> will range from anywhere between -100 (all Detractors) to +100 (all Promoters).</p>
	
	<p>Research America works with your organization’s marketing team to find your brand’s most loyal supporters and identify individuals you can convert into fans. <strong>Net Promoter Score</strong> questions are easy to incorporate into any survey, and our consultants will show you the best ways to use this metric in your research.</p>
