	<h1>
Research America Helps Study<br> Gerrymandering in Virginia</h1>
	<p><strong>Newtown Square, PA, October 30, 2019 </strong>– Gerrymandering is defined on dictionary.com as “the dividing of a state, county, etc. into election districts so as to give one political party a majority in many districts while concentrating the voting strength of the other party into as few districts as possible.” And one of the biggest questions in Virginia politics these days is whether the <a href="https://www.washingtonpost.com/local/virginia-politics/non-partisan-redistricting-sought-in-virginia-as-gop-continues-defense-of-map/2018/11/29/be9c858c-f3fa-11e8-aeea-b85fd44449f5_story.html"> <em>proposed constitutional amendment to block partisan gerrymandering</em> </a>will ever get to the voters. </p>

	
<p><em><a href="http://www.umw.edu/news/wp-content/uploads/sites/7/2019/09/Topline-3-2019.pdf">A new statewide study</a></em> – conducted in September by Research America Inc. for the University of Mary Washington – suggests that supporters of this proposed constitutional amendment have not been effective in promoting it to residents of the commonwealth.
In the study, UMW asked more than 1,000 VA residents whether or not they favored taking the power to draw legislative lines away from the state legislature. The response was a virtual dead heat: 42% favored the constitutional amendment, 40% opposed it, and the rest were undecided. </p>


<p>Despite all the attention the topic has received of late, the current results were not much different from last year. In September 2018, 40% favored the constitutional amendment and 45% opposed it.


</p>
		
		
<p>As Republican legislative majorities have shriveled, and as the party has lost a series of statewide elections, it has become more supportive of anti-gerrymandering legislation.
And although a new Democratic majority might be tempted to keep that power for its own use, that would undermine its long-term commitment to greater citizen empowerment. 

</p>
		
		
		
