<h1>Crisis Communications Experience</h1>
	<p class="green-ques">Submitted by Rex Repass<br>July 10, 2019</p>
	<p>	<img src="../_img/crisis-communications-reputation-litigation.jpg" width="374" height="268" class="focus-group-facilities-1">  </p>
	
    <p>
      
      
      On December 2-3, 1984 there was a gas leak at the Union Carbide plant in Bhopal, India that manufactured pesticides.  This leak resulted in what is considered to be the world’s worst industrial accident.  More than 500,000 people were exposed to methyl isocyanate (MIC) gas. </p>
      
    <p>  The highly toxic substance made its way into and around the small towns located near the plant.   Estimates vary on the death toll. The official immediate death toll was 2,259. The government of Madhya Pradesh confirmed a total of 3,787 deaths related to the gas release.   </p>
      
   <p>   Others estimated that 8,000 died within two weeks, and another 8,000 or more have since died from gas-related diseases.  In addition, a government statement of facts in 2006 stated that the leak caused 558,125 injuries, including 38,478 temporary partial injuries and approximately 3,900 severely and permanently disabling injuries.   </p>
      
     <p> The only other Union Carbide plant in the world that made MIC was in Institute, West Virginia near the state Capitol in Charleston.  </p>
      
    <p>  In the early 1980s, approximately 200,000 people lived in the Charleston metropolitan area, and Union Carbide was one of my clients.  Our work previously had been attitude surveys about Union Carbide’s role in the Charleston community, the company’s involvement in charitable organizations, and an occasional employee survey or focus group.  </p>
      
    <p>  However, after the Bhopal leak and media reporting about the plant near Charleston as the only other chemical manufacturing facility in the world that made MIC, I received a call as Carbide’s regional public relations counsel, Charles Ryan Associates.  They asked if I could help Carbide and the agency with community research about the issue and Carbide’s response.  </p>
      
   <p>   This would be my first experience with crisis communications research to evaluate public perceptions about a corporation, it’s crisis management plan, and the effectiveness of the company’s messaging strategies to address public concerns.  </p>
      
     <p> Since that experience early in my career (six years after completing graduate school), we have led conducted research for many Fortune 500 clients and their public relations and legal counsel on crisis topics ranging from consumer product recalls, product liability, environment issues to education reform, transportation safety, and the 2008-09 financial crisis.  We have learned a lot about the dealing with crisis issues and how to communicate with stakeholders including:</p>
      <ol>
<li>People will forgive almost anything except gross negligence, cruelty and arrogance.</li>
<li>	The most common mistake in the early stages of a crisis is stating something that later proves to be untrue or misleading.</li>
<li>Media and government gravitate to crises that symbolize a larger societal problem. </li>
<li> 	Something doesn't have to be new to become newsworthy.</li>
<li>	Assume everything will be subject to legal discovery. </li>		  
		  
<li>	A crisis is a bad time to try and make new friends. </li>
<li>		Facts and statistics are only effective if presented as part of an interesting story. 	</li>	  
  <li>	he public often reaches a verdict on a crisis long before all the facts are in.</li>
		  <li> 	Society's unwritten rules often change without warning.</li>
		  
		  <li>Remember the chaos theory: unusual events tend to cluster. </li>

          </ol>
	

      
  <p>   During the past 35 years, I have learned many lessons about how the public feels about many good and well-meaning organizations deal with a serious crisis situation that if not managed properly will damage not only the overall reputation of the company, but also severely impact the financial viability of an organization and the careers of people within making decisions about moving forward.  </p>

