	<h1>Quality Assurance Program</h1>

	<p><img src="../_img/quality-assurance.jpg" alt="Quality Assurance Program"  class="focus-group-facilities-5"></p>


	<p>Research America’s incorporates our Quality Assurance program throughout the entire research process to make sure that the data that we collect for your organization reflects the answers only of your targeted
    respondents, and that it is genuine and reliable. </p>

    <p> We apply the most extensive quality control and assurance checks throughout our standard operating procedures to assure our clients of the reliability of our results. We strictly comply with National 
    Committee Quality Assurance (NCQA) guidelines for medical surveys (a higher level than financial quality assurance standards), and apply those standards in our multi-step approach to Quality Assurance. We deploy
     quality, logic and plausibility checks at every step of the research process, managed by your central project manager. She or he works jointly with all members of the research team to deliver reliable, 
    informative, actionable data for your organization. </p>

   <h3>Questionnaire Quality</h3>
    <ul class="check">
 <li>Sample specifications</li>
	<li> Quotas and quota assignment logic</li>
	<li>	Verify Study qualifications against non-qualification logic</li>
	<li>	Skip logic</li>
	<li>	Validation logic</li>
    <li>	Decision Analyst standards</li>
	<li>	Design and flow; questionnaire understandability for interviewers and respondents </li>
	<li>	Elimination of leading/ biasing questions or sequences</li>
	<li>	Grammar, punctuation, and spelling review</li>
	<li>	Consistency (e.g., answer-numbering, answer choices matching question text, consistency in brand-name references)

 
    </ul>


  <h3>Project Review</h3>
<p>  Research America team members involved with your project meet to assign tasks and set goals ensuring that everyone working on the project fully understands the project details and assigned responsibilities for your study.
  We review all quota information, sample specifications, questions, answers, logic, and instructions during this final review before your study is programmed into our systems. </p>
   
Often, team members notice potential threats or risks to the project, and the Quality Assurance Team applies focuses attention and scrutiny in these critical areas.</p>

  
<h3>Survey Program Review </h3>
<p>Once Research America received approval for your questionnaire, our programming team works to implement your survey in our software platform. Quality Assurance team members evaluate the programs accuracy 
 including testing the following areas:</p>
<ul class="check">
	 <li>	Skip logic </li>
	 <li>	Validation logic </li>
	 <li>	Qualification and non-qualification logic </li>
	 <li>	Quota assignments
	 <li>	Flow and understandability</li>
 <li>	Consistency (e.g., answer numbering, answer choices matching question text, consistency in brand-name references) </li>
 <li>	Grammar, punctuation, and spelling </li>

</ul>

<h3>Project Pre-Test </h3>
<p>Research America frequently conducts test surveys to evaluate the questionnaire’s logic. Quality Assurance cross-tabulation and evaluates the correctness of: </p>
 <ul class="check">
	<li> 	Base sizes for all questions </li>
	<li> 	Base sizes for survey quotas and counts </li>
	<li> 	Question text and answer text </li>
	<li> 	Other specific base information </li>
</li></p>
</ul>

 
<h3><strong>Coding Quality Assurance</strong></h3>
<p>Cross-tabulations are checked for correctness by Quality Assurance for the following once survey data is tabulated:</p>
<ul class="check">
	<li> Base sizes and total rows </li>
	<li> Response bases, including open-ended codes and nets </li>
	<li> Numbers match online reporting or marginal data </li>
	<li> Summary tables match individual tables </li>
	<li> Mean information </li>
	<li> Significance testing </li>
	<li> Banner labeling and specification logic </li>
	<li> Table and base titles </li>
	<li> 	Banner points match source tables across all banners </li>
	<li> 	Table specifications file (e.g., table filters for correct bases) </li>
	<li> 	Inconsistent data (e.g., nonusers in a user question, males under a female banner point) </li>
	<li> 	Weights (if present) </li>
	<li> 	Weighting compared to Research America standards </li>
<li> 	Corrections (if necessary) </li>
</li></ul>

<h3><strong>Report and Presentation Review</strong></h3>
<p>We check to ensure that the information that we present to you is meets our high standards, paying attention to all details: </p>
 <ul class="check">
	<li> 	Numbers match source tables </li>
	<li> Graphs, charts, tables, match report numbers</li> 
	<li> 	Written findings match report numbers</li> 
	<li> 	Source information correct on each slide</li> 
	<li> 	Footnotes reflect charts, graphs, table data etc.</li> 
	<li> 	Management summary matches data in full report</li> 
	<li> 	Grammar, punctuation, and spelling</li> 
<li> 	Formal language flow check (unless informal language is specified by a client)</li> 
<li> 	Consistency of formatting in report (document, slide deck)</li> 
	<li> Report’s content and substance</li> 
	<li> Ease of readability by clients</li> 
</ul>

 <h3>Review of Internal Standards and Training Manuals</h3>
<p>Quality Assurance Leadership ensures that Research America’s internal standards and training manuals are up to date. The department reviews these documents for the following: </p>
 <ul class="check">
	<li> 	Workflow and processes</li> 
	<li> 	Grammar, punctuation, and spelling</li> 
	<li> 	Information accuracy</li> 
	<li> 	Clear, concise language</li> 
	<li> 	Current checklists </li> 
</ul>

