	<strong> Project:</strong> Children’s medicine – product development</p>
	
	
	
	<h2>Background</h2>		
 <p>Client intended to introduce a dual-action pain reliever for children and needed to know what it should ‘look like’ (color, consistency, flavor, etc.). Objectives were to:</p>
<ul check>
		<li>	Uncover key consumer expectations for this product</li>
	<li>	Establish a benchmark for specific product attributes (e.g. look, taste, etc.)</li>
	<li>	Assess the prototypes’ performance on specific physical attributes </li>
</ul>



<h2>Approach</h2>	
	
<p>	Eight focus groups were completed among mothers of children age 2-6 years old who have dosed child with a liquid analgesic in the past month. Moms were first exposed to a concept and then ‘pour-tested’ seven products (four current competitive offerings and three prototypes.) </p>


	
	<h2>Results</h2>
	
	<p>Consumers expect the dual-action product experience to be different from current products. This research helped the client to determine the most desirable consistency, color, scent, and flavors for the product.</p>

	
