	<h2>Research America Opens Orlando Central Location Testing Facility
 </h2>

	<h3>Expansion into Central Florida Includes Area Market Research Expertise
</h3>

<p><b>Philadelphia, Pa. </b> – Research America announced the opening of its Orlando Florida Central Location Testing facility.   The company’s Altamonte Springs office is the latest expansion for the Philadelphia-based company, joining its large New York City metropolitan area product testing and focus group center. 

</p>
     
<p>Research America has tapped Consumer Connections’ Maddie Sutphin to lead its Orlando facility.  Maddie's 35-year experience in marketing research has been concentrated on CLT testing for and quality control for the Food/Beverage and Fragrance industry at the Fortune 100-level. The new Orlando facility features more than 10,000 square feet of space for market research central location tests and focus groups.  
 </p>
		
<p>“We’re proud to be able to connect brands with their southeastern consumers, especially in central Florida,” Robert Porter, Research America’s CEO said.  “The Orlando-Kissimmee-Sanford market continues to grow its population and business opportunities, and our clients want to check the pulse of adult consumers here.  Maddie knows her community well, and her recruiting expertise will be a valuable asset to our clients. I’m glad that she has agreed to join Research America.”</p>

<p>The Central Florida space offers a large focus group room and personal interviewing spaces, client monitoring facilities, a large contemporary test kitchen, laundry testing areas, and dedicated sensory testing rooms.  In addition to the new Orlando area facilities, the company operates additional focus group and interviewing locations in Sacramento, California and Detroit, Michigan.  Research America’s larger client service centers also provide web-based and telephone data collection capabilities to clients. </p>

<p>The new facility, located in Altamonte Springs, features customizable spaces which can accommodate 12 to 40 respondents. Many of the rooms are multi-purpose and can be configured to simulate various retail environments.   Eight specialized, fragrance testing rooms are on site. Each room is environmentally controlled to provide the optimal environment for sensory testing.</p>

	
	
