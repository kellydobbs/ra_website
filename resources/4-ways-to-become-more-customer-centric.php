	<h1>4 Ways to Become More Customer-Centric			</h1>
	<h3>Opportunity – “Your most unhappy customers are your greatest source of learning.” – Bill Gates</h3>

<p>Identifying opportunity is a crucial step in changing and growing your business. Your customers can help you grow, both through their actions and their feedback.</p>

<p>&nbsp;</p>
<p>Here’s what customer-centricity is in the simplest terms: It’s listening and learning. It’s leveraging information and knowledge about your customer for business action. Actions that put the customer first and delight them so they return and help spread your message. Customer-centricity is an organizational discipline, not an individual discipline.</p>

<p>Some companies struggle to make time to listen to their customers. Others can find it difficult to take the right action.</p>

<p>The path to customer-centricity is doing both – listen to and acting on what customers say.</p>

<p>It may help to think of it as a matrix –</p>
	
	
	
	<p><center><img src="../_img/blog-3.png"  alt="4 Ways to Become More Customer-Centric"/></center></p>
	
    <p>
    <b>Customer-Centricity:</b> If you listen to customers and act on that feedback, their problems and pain points become opportunities. If you’re the first to address these opportunities, you gain a competitive advantage. But, that doesn’t mean you stop listening and acting. Stay disciplined and build the capacity to be even more customer-centric.</p>

<p><b>Chaos:</b> All businesses must be profit-oriented. However, when a company takes action without first listening and knowing its customer, understanding their journey, or uncovering how a change might affect them, there is chaos and a waste of resources.</p>

<p><b>Cautious:</b> On the opposite end of this spectrum is listening without action. There needs to be balance. At some point, listening and learning has diminishing returns. You need to apply that knowledge through action – then you can learn from that action.</p>

<p><b>Crashing:</b> It may go without saying, but if you’re not listening and you’re not taking new action your business path is a downward spiral. </p>

<p>Hopefully, you now have a good sense of what quadrant your business is in. And, more importantly, how to get to the upper right…or further up and to the right. </p>

<p>We believe any customer listening or market research program should motivate action. Sometimes it’s by uncovering new ground, building alignment, building confidence in an idea, or leveraging data to build KPIs and motivate teams. </p>

<p><strong>There are four ways we often talk about that help organizations begin the process of becoming more customer-centric. They are:</strong></p>
<ol>
  <li>
    
 	Constantly Gather Information. The more you know about your customers, the better you can anticipate needs and opportunities. Of course gathering the data is only part of it. Knowing how to use it is the other part.</li>
	  
  <li>	Get Embedded in Their Strategy. To forge a deeper relationship, focus on how your product/service is part of your customer’s life. Make them the hero. How does your strategy support your customer’s strategy?</li>
	  
  <li>	Emphasize Customer Retention. It’s always easier and less expensive to sell to existing customers. Leverage research and data to monitor customer retention and identify areas at risk or opportunities for building deeper loyalty.</li>
	
   <li>	Debrief After Customer Engagements. Anytime you or someone with your team (i.e. sales) has interactions with customers, there’s an opportunity to learn, formulate new questions, and to be honest about strengths and weaknesses. Build this rhythm of reflection into your organization.</li>
    
  <p>  When it comes to leveraging customer data and feedback to uncover new opportunities, there are two different approaches – the candlelight and flashlight approach. </p>
    
 <p>	The candlelight approach is a systematized, ongoing program that collects customer data and feedback. This typically looks like a voice of the customer program, a robust CRM program, or ongoing customer satisfaction and tracking research. With these programs you can obtain glimpses into what’s happening with the business, in the market, and with consumer perception. They also help to create a culture of customer-centricity. </p>
    
<p>	However, there are may be many areas of the business where you may need to shine a light on the problem to uncover new opportunity. This is the flashlight approach, and it typically involves a more focused research initiative – often custom – that looks at a specific problem or area of the business in depth. </p>
    
   <p> If you’re looking to put the customer first while identifying opportunities for your brand, here are some specific types of research to consider:</p>
	
	<ul>
    <li>	Brand equity assessment</li>
	 <li>	Competitive intelligence</li>
	 <li>	Market/category structure</li>
	 <li>	Consumer/shopper segmentation</li>
 <li>	Habits & practices, attitudes, usage</li>
    <li>	Journey mapping</li>
   <li>	Customer Satisfaction</li>
    </ul>
	
 <p>   <strong>Customer-centricity</strong> has become the new competitive advantage in today’s increasingly complex and globalized world. Developing the right systems, processes, and partners for putting the customer at the center of every decision will put your team and your company at a significant advantage for success. Working with the right partner will help you create a clear link to value creation by defining the outcomes that really matter, analyzing historical performance of satisfied and dissatisfied customers, and focusing on customer satisfaction issues with the highest payouts. This requires discipline and patience, but the result will be early wins that will build confidence within the organization and momentum to innovate further.</p>
    
    
    
  </li>
</ol>
