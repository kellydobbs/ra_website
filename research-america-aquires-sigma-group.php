	<h1>Research America Inc. Acquires Sigma Group</h1>
	<h2>Philadelphia, July 7, 2016</h2>
	
	<p>Research America, a privately owned marketing research company comprising eleven United States locations, completed its acquisition of assets of Sigma Group, LLC, based in Lincoln, Nebraska.</p>

	<p>Sigma Group, founded by Dennis Nutter in 1998, is a leading specialist in healthcare marketing research, specializing in delivering several forms of satisfaction surveys and community image and preference market surveys for hospitals and healthcare systems. Sigma Group serves clients throughout the United States, ranging from small community hospitals to larger metropolitan healthcare networks, clinics and health plans .</p>

	<p>Research America’s President, Robert Porter, remarks, “As a company that places emphasis on attentive customer service, Sigma Group certainly is a good-fit acquisition through its traditional emphasis on ‘hand-crafted marketing research’ studies for its clients. I’m confident that acquiring Sigma Group will add to our organization’s capabilities in serving healthcare communities throughout the United States. I’m looking forward to working with Dennis Nutter and the other members of the Sigma Group team as we continue to expand our healthcare and pharmaceutical research capacities.”</p>

	<p>This is the fifth successful acquisition by Research America during the past year. With the addition of Sigma Group, Research America can offer its current healthcare and pharmaceutical customers additional, in-depth research services to better understand their marketing challenges to their immediate communities.</p>

	<p>Research America continues to expand its network of local marketing research centers by evaluating opportunities to open local, branded offices throughout the United States. The firm has established a network of marketing research facilities based in its headquarters in Newtown Square, Pennsylvania. The company is in the process of expanding its pharmaceutical and healthcare marketing research division through two forthcoming acquisitions. It offers energy and utility companies dedicated marketing research specialty expertise through Metrix Matrix, located in Rochester New York. The company owns a central testing location facility in Totowa, New Jersey.
	About Research America</p>

	<p>Research America is a nimble, value based public opinion market research firm providing organizations with market research needed to enhance products and services for their customer base. With 30 years’ experience and a network of nationwide offices, the company provides quantitative and qualitative services delivering interviewing services to a variety of communities around the world. Research America team leaders are experts in coding analytics and surveying.</p>
