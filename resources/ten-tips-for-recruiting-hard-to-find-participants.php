<h1>10 Tips for Recruiting Hard to Find Participants (Hint: Get Creative!)</h1>

		<p>Without a doubt, recruiting the “right” participants is crucial to the success of any qualitative research project. Yet, any experienced researcher will tell you recruiting is one of the most difficult parts of the qualitative research process! 

</p>

	
	<p>Potential participants have become increasingly concerned about being involved in qualitative research due to privacy, security, personal safety, and even cultural concerns. And the situation is exacerbated if you are working on a project that could generate additional concerns, such as projects pertaining to sensitive and private topics (e.g., finances or sexuality), illicit activities (e.g., drug use) or if your required participant is elusive or less frequent (e.g., high net worth individuals or orthopedic surgeons). But even researchers with less challenging topics find recruiting good qualitative research participants can be difficult.</p>

	
	<p>Our goal was to predict what people wanted before they realized they wanted it, which meant the new methodology needed to get at latent emotional triggers (implicit in system 1 thinking). However, we also wanted to ensure in-market predictability which is best measured through the wisdom of crowds technique. Finally, we wanted it to work on a stand-alone basis or integrate seamlessly into an existing concept testing program. 

</p>
	
	
<h2>Do Your Homework</h2>
<p>Two pre-conditions will make recruiting easier or, at least, less challenging. The first is to allow enough time for recruitment. If you cannot allow a full two weeks, you will have difficulty recruiting, especially if you have a low-incidence population. Second, define your desired participants, and make sure your recruiting screener can identify them accurately. Ask yourself:</p>



	
	<ul>
	
	<li> To whom do we want to talk? </li>	
	<li> What exact characteristics and behavior should they have? </li>	
	<li> Whom do we want to exclude? </li>
			
	</ul>

<p>
	Don’t take anything for granted, even if your client claims their database is perfect (which it never is)! If a participant must have a specific characteristic or behavior, confirm this with a screening question to be sure they qualify. </p>

<p>One of the most common methods of recruiting qualitative participants is to rely on your research partners’ permissioned database. But if you’re facing a difficult or low-incidence recruit, you may need to get creative. Think about the qualitative methodology you are using (e.g., online, in-home, CLT, in-person focus group, phone IDI) and where you might find your desired population of participants, and the implications of that for your recruit. </p>

	
	
<h2>Recruiting Methods Beyond the Database </h2>	
	
	
<p>Creativity pays off if it helps you find the right participants. Here are some ideas to spark your creative recruiting thinking.</p>
	
	
	<ol>
	  <li>	<strong> Whom Do You Know? </strong>If you are working with a firm that has a database of potential participants, ask them to query their members to get referrals to other qualified individuals. You might even send an email to your contact list asking colleagues and friends to forward your email to potential participants. </li>
		
	<li><strong> Door-openers.</strong> This is similar to asking your contact list for referrals, but in this case, you go to specific individuals who influence the people to whom you want to talk. For example, are you looking for participants of one particular religion? Ask a well-known pastor if they would recommend your study to the congregation. 
	
	<li> <strong>Leverage social media.</strong> If you or your research partner have a social media presence, use that to find the right participants. In addition to Facebook, don’t forget to think about LinkedIn for B2B projects. Look at Facebook or LinkedIn groups that might be appropriate for your recruit. And don’t forget about bloggers, especially for harder to find populations.</li>	
		
		
	<li><strong>Forums, groups, and communities.</strong> Are there any organizations that might be interested in your research project to the extent that they might advertise it to their members? Especially when you are looking for people with a specific behavior, belief or attitude, these groups can be excellent targeted recruitment tools.</li>
	
	<li><strong>Direct mail. </strong>Although this may seem archaic, if your project requires people in a particular geographic area, this technique might work for you. Put together a compelling postcard and screen everyone who responds.</li>
		
	<li><strong>Buy an email list and solicit via email.</strong> Similar to direct mail, this approach might work whether or not you have a geographic limitation. </li>
		
	<li><strong>Flyers.</strong> As the saying goes, “Fish where the fish are.” Do you want people who purchase dog food from a particular store (perhaps your competitors)? Go to that store and give out flyers or stick them under the windshield of nearby cars. </li>
		
		
	<li> <strong>	Local newspaper ads.</strong> Running an ad in the local newspaper is similar to direct mail in that you are reaching people within a geographic area, such as for in-person qualitative research.</li>
		
		
	<li>	<strong>Become an exhibitor. </strong>Depending on your participants’ qualifications, you might be able to set up a table at a local fair or conference and solicit attendees to take part in your qualitative research.</li>
		
	<li><strong>In-Person Recruiting. </strong>With the location’s permission, you might even be able to recruit qualitative research participants in person, by intercepting them in stores, restaurants, parking lots, or public places. </li>
			
    </ol>
	
<p>There are probably many, many more creative ways to recruit qualitative research participants. After all, you are limited only by your imagination. And remember: the recruiting process itself can yield essential insights about the population of interest. So go ahead, get creative!</p>

