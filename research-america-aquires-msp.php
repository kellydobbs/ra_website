	<h3>Philadelphia, June 24, 2016</h3>


<p>Research America, Inc., the fastest growing marketing research and opinion polling organization in the United 
States, has confirmed a vital acquisition in its quest to further develop its capabilities for pharmaceutical 
clients. In a deal finalized May 13th, the company acquired MSP Analytics, Inc., located in Willow Grove, 
Pennsylvania.  </p>

<p>This acquisition will strengthen and broaden Research America’s existing capabilities in providing marketing 
research for the pharmaceutical industry. The purchase of MSP Analytics is a definitive move that compliments 
their expansion plan. Confirming their commitment to their new and present clients, the integration of both
 companies is now underway. </p>

<p>This transaction marks the sixth successful acquisition for Research America during the past year. </p>

<p>The MSP Analytics team is in the process of moving its staff and capabilities into new offices, located near
 its former location in Willow Grove. </p>

<p>MSP Analytics was founded in 1999, making its mission understanding and predicting human behavior 
in the context of the global, commercial healthcare environment. Senior executives Dr. Paul Bugos,
 Dr. James Cohen will assume key directorship positions within the company. </p>

<p>Research America increases its capability to support in-depth research studies and data collection 
through integrating MSP Analytics’ experience and solutions into its existing infrastructure. 
Company leaders have played industry roles as both consumers and creators of pharmaceutical marketing research. </p>

<p>Research America President Robert Porter comments: “Expanding our capabilities in pharmaceutical marketing
 research has been an important goal for us. Dr. Bugos, Dr. Cohen and their team bring their deep knowledge of 
 specializations within this field to our company, supporting our efforts to develop this business area.
  We’re looking forward to working together in providing research that ultimately provides patients with knowledge, 
  and helps build relationships of trust between pharmaceutical companies, healthcare providers and the
   patients who rely on their products.” </p>


<p>Research America continues to expand its network of local marketing research centers by evaluating 
opportunities to open local, branded offices throughout the United States. The firm has established 
a network of marketing research facilities based in its headquarters in Newtown Square, PA and branching 
out to Philadelphia, Lehighton and Pottsville, PA. It operates a facility almost entirely dedicated to 
political research and public opinion polling in Sacramento, CA. </p>

<p class="green-ques">About Research America </p>

<p>Research America is a nimble, value based public opinion market research firm providing organizations 
with market research needed to enhance products and services for their customer base. With 30 years’ 
experience and five nationwide call centers, the company provides quantitative and qualitative services 
delivering interviewing services to a variety of communities around the world. 
 Research America team leaders are experts in coding analytics and surveying.</p>












	
