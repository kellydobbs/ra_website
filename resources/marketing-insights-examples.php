<h1>3 Surprising Marketing Insights Examples</h1>
<p>Are you a good listener? Here at Research America, we pride ourselves on our listening skills. After all, <a href="https://researchamericainc.com/resources/Quantitative-Qualitative-Market-Research.php" ><span>market research</span></a> depends on how well we pay attention to the things consumers say and do. Only by listening and questioning and examining consumers are we able to condense, prioritize and analyze data, recommending actions that lead to smart <em>and</em> profitable results for the companies we serve.</p>

<p>Part of being a good listener is remaining open to the discovery of things unknown. It’s also Market Research Rule #1. Too frequently, companies — and the market researchers they hire — set out to prove a theory or validate a hunch. While having a specific question in hand at the onset of any research endeavor is critical to the project’s success, it’s important for researchers to not overlook surprising outcomes simply because they don’t expect or agree with them. Oftentimes, it’s the unexpected marketing insights that lead companies to some of their greatest successes. Take a look at these surprising marketing insights examples from some of the world’s most well-known businesses:</p>
<p>&nbsp;</p>
<h2 class="med-link">DirectTV</h2>
<p>Using market research, DirectTV was able to determine a link between new <a href="https://www.martechvibe.com/insights/staff-articles/data-driven-marketing-is-critical-for-brands/" target="_blank"><span>homeownership and new television services</span></a>. Surprising? Only if you aren’t open to digging deep for unusual correlations. With this insight, DirectTV was able to apply USPS information to identify homeowners registering a change of address, targeting them with personalized marketing materials and ads in an attempt to upgrade or change their TV subscriptions.</p>
<p>&nbsp;</p>
<h2 class="med-link">Mars, Inc.</h2>
<p>Want to guess why people in Russia didn’t eat Mars ice cream during the summer right after the collapse of communism? (Even if you want to guess, don’t.) When Mars went to Russia to ask consumers why they weren’t buying ice cream during the logical ice cream season, its executives were shocked to learn that Russia didn’t have enough refrigerated trucks to <a href="https://www.tampabay.com/archive/1992/02/09/moscow-needs-a-supermarket/" target="_blank"><span>supply store-bought ice cream for home consumption</span></a> and some households <a href="https://www.adamsmith.org/research/back-in-the-ussr" target="_blank"><span>didn’t even have a refrigerator</span></a> at home at all! In the summer, all the ice cream would melt before reaching shelves/homes. All the marketing in the world can’t fix a problem like that! Thus, it’s good Mars was willing to listen to consumers; otherwise, it never would (or could) have resolved the issue.</p>
<p>&nbsp;</p>
<h2 class="med-link">Dove</h2>
<p>Historically, Dove has been known as a “beauty bar.” But in 2004, when consumers were asked about the concept of beauty, half of the women surveyed believed beauty was too narrowly defined and just <a href="https://www.huffpost.com/entry/dove-real-beauty-campaign-turns-10_n_4575940" target="_blank"><span>2%</span></a> believed themselves to be beautiful. As a soap company, Dove could have chosen several other ways of interpreting and using this information. Instead, it revolutionized the advertising industry, successfully changing cultural perception with their resulting “<a href="https://www.youtube.com/watch?v=R5Ynz3eje9s" target="_blank"><span>Campaign For Real Beauty</span></a>.”</p>
<p>&nbsp;</p>
