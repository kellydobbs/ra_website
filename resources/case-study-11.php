	<h2>The Client</h2>		
 <p> A leading Engineered Wood Manufacturer had developed a new flooring underlayment that combined industry-leading strength, superior moisture resistance, and installation ease.</p>

<h2>The Problem</h2>	
	
	<p>  The client was having a difficult time driving conversion away from the age-old industry standard that was cheaper and widely available, but less effective. Therefore, defining potential barriers to conversion and identifying strategies to overcome them were important factors in finding potential growth opportunities…. specifically, to understand what drives competitive users to choose plywood, define what barriers exist for the engineered wood product, and establish pricing demand thresholds by consumer segments.

</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>Utilizing a multi-phased approach that involved <a href="https://researchamericainc.com/services/qualitative-marketing-research.php">qualitative</a> and <a href="https://researchamericainc.com/services/quantitative-data-collection.php">quantitative techniques</a>, attribute drivers and deflectors, usage behaviors, and performance superiority/inferiority were identified to help develop new positioning statements.  A final quantitative phase developed pricing demand curves around the optimized positioning.</p>

<h2> The Outcome </h2>
	
<p>The client developed and launched a targeted plywood conversion program across six states resulting in 27% volume growth for their product vs. the 19% volume growth average across the country.</p>
	
