	<p>Short answer: it's the law. The Lanham Act of 1946 is the federal statute that governs trademarks, service marks, and unfair competition (including advertising regulation). Administered by the<b> Federal Trade Commission</b>, the Lanham Act's purpose is to prevent false and misleading advertising as well as to </p>
	
	
	<p class="med-link">Other organizations who have a role in ad claims regulations include:</p>
	
	<ul>
	
	<li>		The National Advertising Division (NAD) of the Council of Better Business Bureaus reviews the claims made in national advertisements in the U.S. and Canada. NAD can offer alternative dispute resolution with voluntary compliance to help marketers self-regulate and avoid costly litigation. </li>
	
	<li>		ASTM International is a global organization that has established standards for more than 12,000 products and services. These standards and guidelines specify how to test products in substantiating ad claims about product formulations.
</li>
		<li>				<b>The Media</b> have long had media guidelines that dictate what kind of advertisements marketers can use. And now, with the proliferation of social media, advertisers are often in uncharted territory and learning about best practices on-the-fly. 
       </li>	

	</ul>
<p>And then we have the courts, where advertisers, competitors, and consumers always have the option to challenge ad claims. The results of individual lawsuits become case law, affecting the future rules of the game, and can also impact what marketers are required to do to substantiate an ad claim.</p>
	
