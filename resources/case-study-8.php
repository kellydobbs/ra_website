	<h2 class="big-link">RAI Case Study #8 </h2>
	<h1>Topic: Repositioning an Organic Ice Cream Brand  </h1>	
	<p>	<img src="../_img/ad-testing.png" alt="" width="396" height="166" /></p>
	<br>
    <P class="med-link">
		The Client
<p>Alden’s Organic Ice Cream has been a customer favorite since its founding in 2004. Sourced from family owned farms, Alden’s ingredients come from cows who enjoy plenty of fresh air, graze a minimum of 120 days a year and who eat feed that is 100% organic. Alden’s customers trust the brand to deliver good tasting, high quality, fulfilling organic ice cream that is clean and progressive, and made with care. Alden’s Organic Ice Cream is certified organic by the U.S. Department of Agriculture.</p>
	
	
				<p><img src="../_img/caseStudy_iceCream_quote copy.png"  class="ice-cream" alt="" /></p>

 <p class="med-link">The Problem</p>
 <p> The U.S. ice cream market will reach nearly $9 billion in 2020. Organic ice creams account for a fraction of that, about $1 billion, but is growing at a rate of 4.5% annually. Unfortunately, as a mainly regional brand with limited distribution, Alden’s is not drawing consumer attention in a freezer case that is increasingly stocked with enticing choices, including other national organic ice cream brands. </p>
 <p>Alden’s needs to reposition its brand to compete effectively in a crowded marketplace with many alternatives in terms of shopping, delivery, packaging, and flavors. The brand needs to prepare for the future by differentiating itself while building on its core strengths and equities. Alden’s wants to own the position that its ice cream is all good, meeting all of the needs of the organic ice cream consumer: taste, high quality, fulfilling, and organic, while also connecting to the consumer on shared values (emotional) and truly intense flavors.</p>
	<p class="med-link">Our Recommendations </p>
 <p><strong>The research objectives were to:</strong></p>
 <ul class="check">
  <li> Assess the performance of the new brand position against the current position. Will it attract target customers without alienating current buyers?</li>
    <li>Assess package performance to current and competitive benchmarks.</li>
   <li> Evaluate the role of a “signature” pint-sized package shape on purchase interest.</li>
   Screen 25 flavor/product descriptions to determine which are most likely to achieve trial, and which combination will maximize penetration and reach.</li></ul>
 <p>Research America recommended a hybrid quantitative-qualitative research methodology. In two markets, we recruited 80 respondents each to participate in four 60-minute quantitative sessions. Using tablets, we tested stimuli iteratively to identify the optimal combination of packaging and flavors. Through the quantitative research, qualified individuals were identified to participate in four qualitative groups of 6 – 8  of 50 minutes in each market. These qualitative sessions then explore individuals’ reasons for their preferences, the “Why?” behind their choices.</p>
 <p class="med-link">The Outcomes</p>
 <p>The research uncovered a new flavor and package profile that maintained all of Alden’s current equities and added strengths, that allowed Alden’s to develop a stronger bond with consumers and a more compelling brand personality to stand out from the crowd in the freezer case. Additionally, Alden’s decided to consolidate its two organic brands into one under the Alden’s Organic brand to take advantage of larger opportunities. Line optimization identified eight core flavors, with the opportunity for seasonal promotions. The new packaging developed through the research highlights the actual product/flavor and had stronger stopping power in the freezer case. Finally, adding a pint-sized package encouraged trials and met consumer needs for variety in flavor purchase.</p>
 <p>Alden’s revitalized brand position, packaging and flavors were successfully introduced to the market and picked up by several national mainstream grocery and mass stores, increasing trial and gaining additional loyal consumers for the Alden’s Organic brand.</p>
