	<h1>Americans Are Optimistic Despite Uncertainity
 </h1>
<p class="med-link">Forty percent believe the world will be better than before the coronavirus</p>
		
<img src="./_img/COVID19-Infographic.jpg" class="large"  alt="COVID-19 - Americans Are Optimistic Despite Uncertainity">
	

<p><b>Cincinnati, Ohio</b>  –  While Americans have a high level of anxiety during the coronavirus pandemic, most are willing to make significant sacrifices and many are optimistic about the future. They also believe that government leaders are taking the appropriate actions, according to a new national Research America, Inc. public opinion poll. </p>
	
<p>Sixty-one percent of Americans say they are “extremely or very concerned” about the coronavirus outbreak, though 13 percent are “not very or not at all concerned.” Nearly half, 47 percent, say the pandemic has had a significant negative impact on their personal life due to closures, cancellations, restrictions and food shortages, said Rex Repass, President of Research America Inc.
	</p>
 <p>“We are at a critical time when many Americans are personally impacted in multiple ways by COVID-19. During the next several weeks many may have a family member or close friend with a  positive coronavirus test,” Repass said. “However, most Americans are willing to adhere to their state’s stay-at-home order if it’s for the greater good, with 69 percent saying they can accept extreme limitations for more than a month if it helps to suppress the infection rate. Two-thirds go so far as to say they don't care how long restrictions are in place if it prevents widespread illness and death.”</p>
<p>On the other hand, about one quarter of respondents, 27 percent, feel the social distancing and other limitations go too far, and only 11 percent said they worry about transmitting the virus to others.</p>

<p>The poll of 600 U.S. adults also found that 75 percent agree with the extreme measures put in place to protect people from the coronavirus. A high number strongly support such measures as mass cancellations and closings (87 percent), travel bans (86 percent), restricted visitation at hospitals, nursing homes, etc. (86 percent) and the closing of borders (82 percent). 

<p>“The poll results also show that altruism is alive and well throughout the U.S. with Americans continually demonstrating selfless acts for the well-being of others,” Repass added. “People are clearly more concerned about a family member or friend getting sick than themselves.”</p>

<p>Nineteen percent are worried someone close to them will die compared to 12 percent who are worried about themselves dying. </p>

<p>Nearly eight-in-10 (78 percent) want politicians to focus on the welfare of the citizens and not  on politics. One in four (25 percent) are concerned that the healthcare system will be overburdened, and people won’t get the care needed.</p>

    <p>The Center for Disease Control & Prevention received the highest job performance rating, 64 percent satisfaction followed by their state’s governor, 56 percent. A plurality (43 percent satisfied/33 percent dissatisfied) with President Donald Trump, 44 percent with Vice President Mike Pence with the balance unsure or having a “wait and see attitude.”</p>   

<p>“This was highly dependent on political party,” Repass said. “As expected, more Republicans were satisfied with President Trump’s handling of the coronavirus pandemic, 73 percent, as compared to Democrats, 22 percent.</p>

<p>Although 20 percent are concerned about the virus causing a economic recession, very few are worried about permanently losing their job, only 7 percent, their company going out of business, 5 percent, or the loss of retirement income from stocks and 401k, 8 percent. More than half, 57 percent, feel confident that their job will be secure throughout the pandemic.</p>
<p>Americans believe there are brighter days ahead with 40 percent saying the world will be a “better place” after the virus has been controlled, compared to 18 percent who believe the world will be worse off.  In a year or two, half believe the world will be a better place because people will have an appreciation for what is really important in life, 54 percent; there will be new advancements made in medicine, 52 percent; and people will be more compassionate, 35 percent. </p>

<p>Actions Americans feel will have the most positive impact include paid sick leave for coronavirus recovery for themselves or to care for a family member, 67 percent; expanded SNAP and other benefits, 67 percent; extended unemployment benefits, 66 percent; and highly subsidized, reduced cost, or free medical services for coronavirus treatment and prevention, 65 percent.
<p>Two in five Americans worry about health care costs increasing post coronavirus pandemic, 42 percent, and having to work longer in life than anticipated to offset money lost during this time, 41 percent.</p> 

<p>The Research America, Inc. Covid-19 Public Opinion Poll was conducted between March 26-29 among a sample of 600 U.S. adults over the age of 18. The overall confidence interval for the survey is +/- 4.0 percentage points at the 95 percent confidence level. </p>
	
	<h2>Methodology Statement</h2>
<p>Results of this Research America, Inc. Covid-19 Public Opinion Poll are based on interviews conducted between March 26-29, 2020 among a national sample of 600 adults over the age of 18 in the United States. Data collection was completed online.</p>  

<p><strong>U.S. adults over the age of 18 in all 50 states</strong> were sampled for the survey and modeled to age and race demographics based on data from the United States Census Bureau. With online data collection it is not appropriate to apply a probability-based margin of error to interviews completed.  However, applying statistical tests of significance to each question asked at the 95 percent confidence interval yields an overall statistical error of +/- 4.0 percentage points based on the 600 interviews.  The 95 percent confidence interval varies by question and based on the distribution of responses to each question.</p>

<p>The purpose of the Public Covid-19 Public Opinion Poll is to provide a snapshot of opinion and timely views about Covid-19. Research America Inc. sponsored this survey.</p>


