	<h1>Telephone Surveys</h1>
	
	<p>Rule number one of marketing: understand your customer. The problem is that many businesses don’t have the time or money to do the in-depth marketing research that helps them understand their customers and their markets. That’s where a marketing research company that specializes in <strong>telephone surveys</strong> and research can help. These marketing research companies help you find out what your customers want through conducting in-depth telephone surveys in your key customer areas and expansion territory.</p>

	<p>Without the help of a marketing research company, it’s hard for businesses to build a complete picture of their consumer’s point of view. Hiring marketing research companies helps businesses understand consumers’ thoughts, feelings, and opinions. Marketing research companies would be able to tell a hospital how they’re doing with customer service, a spa which products consumers like the most, and an electronics chain which gaming systems are going to be the next hot seller.</p>

	<p>In order for businesses to understand the mind of the consumer, marketing research companies first have to create a customized marketing research survey. When creating a survey, marketing research companies do more than simply generate a series of questions with yes/no answers. To develop the questions, marketing research companies use many of the different marketing research methodologies that would be most effective for their client’s business. For example, if a business needed to know how frequently consumers use their products, a marketing research company could incorporate the quantitative marketing research question of “How many times a month do you use this product?” If the business wanted to know why consumers use the product, a marketing research company could include the qualitative market research question of “How does it feel to use this product?” Both quantitative and qualitative questions are valid marketing research strategies that are used according the business’ needs.</p>

	<p>Once the customized marketing research survey is designed, the marketing research company gets to work surveying consumers. This is usually done by telephone, since many consumers respond better to real voice rather than an impersonal internet survey they can click to ignore. The marketing research company will usually use a list of randomly generated telephone numbers, representing the general population, to conduct the survey, unless a specific demographic is requested. This random sampling ensures that their clients receive unbiased marketing research data with a minimal margin of error.</p>

	<p>After marketing research survey data is collected; the client turn to an independent marketing research analyst to interpret the results, or use an analyst from the same marketing research company that conducted the survey. Either way, the marketing research analyst will give cohesive analysis of the results, helping the client take the next step in developing and selling their products and services.</p>

