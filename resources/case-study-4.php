	<h2 class="big-link">RAI Case Study #4</h2>
	<h1>Topic: Optimizing Brand Communications</h1>	

		<p><img src="../_img/RAI-case-study-4.jpg" alt="Market Research - Optimizing Brand Communications" width="397" height="147" />
			
	<h2>The Client</h2>		
 <p>  A major drug manufacturer that needed to select winning print executions for a DTC (direct-to-consumer) campaign for one of their prescription drug products.</p>

<h2>The Problem</h2>	
	
	<p>   The client wanted to evaluate the effectiveness of each advertisement based on its performance across metrics like stopping power, communication, engagement, impact and persuasion and compare them to pharmaceutical industry norms.</p>
<p>Further, they wanted to assess each creative execution to analyze the impact of visuals, characters, copy, taglines, main message, and feelings/emotions triggered by the ads, as well as identify any questions/concerns raised, especially wi
</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>A multi-phase research program combining exploratory <a href="https://researchamericainc.com/services/qualitative-marketing-research.php">qualitative research</a> and a central location test (CLT) blended quantitative-qualitative methodology. 
 </p>
	<ul>
	
	<li> 		<strong>Phase 1</strong> – Exploratory qualitative that reduced the number of creative print executions from five to two, and to identify optimization opportunities was completed.</li>

	
<li><strong> 		Phase 2</strong> – Following exposure to the optimized ad via a clutter magazine, patients completed a quantitative survey (using iPads) to identify the winning execution; quant was followed by qualitative breakout sessions that provided in-depth understanding of quant results. </li>
	</ul>
	
<h2> The Outcome </h2>
<p>	This research confirmed the chosen print ad execution was attention-grabbing, informative and relevant to patients, and strongly motivated them to seek additional information about the drug, highlighting the importance of including two additional benefits outside of the core benefit, and identifying patient segments most receptive to the messaging in the creative execution. </p>
<p>The client launched the DTC ads immediately after the research. Sales of the prescription drug increased 35% in the year following launch. </p>

