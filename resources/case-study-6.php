    <h2>The Client</h2>		
 <p>  A pharmaceutical company wished to jump-start lagging sales of a new prescription drug via a direct-to-consumer (DTC) advertising campaign.</p>

<h2>The Problem</h2>	
	
	<p>    Little to nothing was known about consumers’ experience with the disease, or their knowledge or perceptions of the drug’s benefits. The client needed to define an overall messaging strategy and 
determine what positioning would best motivate consumers to talk to their doctors about the new drug.    

</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>A multi-step research plan utilizing a blended quant/qual approach was defined to build foundational consumer knowledge and develop, refine and optimize the best positioning statement for the brand… all in a one-week time frame.  
</p>
	<ul>
	<li><strong>Day 1</strong> – Using Storytelling techniques in qualitative discussions, consumer insights about their disease experiences, information needs and desired product benefits were assessed.</li>

	
<li> 		<strong>Day 2</strong> – The individual components (insights, benefits and reasons-to-believe) of a brand positioning statement were evaluated, revised and retested. </li>
		
		
<li> 		<strong>Day 3</strong> – We led the research team in a “concept lab” where high scoring concept components were combined into potential positioning statements. </li>
		
<li><strong> 		Day 4</strong> – Initial positionings were tested (quantitatively and qualitatively) and revised with consumers. </li>
		
		
<li><strong> 		Day 5</strong> – The highest scoring positioning statements from Day 4 were then validated by consumers. </li>
		
		
	
	</ul>
<h2> The Outcome </h2>
<p>The highest scoring positioning statement from the research was used to develop a DTC ad campaign that launched three months after the research was completed.  Following three consecutive ‘flat’ quarters, sales increased 42% in the first quarter after the campaign was introduced… and more than doubled (+124%) in the year following the launch the DTC campaign. 
