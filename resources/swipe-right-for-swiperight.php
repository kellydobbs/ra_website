<h1>Swipe Right for SwipeRight</h1>

		<p>The gold standard for successful marketing research is to predict market behavior accurately, and of course, result in-market success. Research America’s team of experts set out to develop a methodology that would help their client better lead their industries through decisions based on more predictive results. 
</p>

	
	<p> Traditional research methods excel at predicting mass appeal products but can fall short on new-to-the-world ideas or highly-differentiated niche offerings. This often leads to companies not moving forward with category-changing new products due to risk aversion. </p>
	
	
	<p>Our goal was to predict what people wanted before they realized they wanted it, which meant the new methodology needed to get at latent emotional triggers (implicit in system 1 thinking). However, we also wanted to ensure in-market predictability which is best measured through the wisdom of crowds technique. Finally, we wanted it to work on a stand-alone basis or integrate seamlessly into an existing concept testing program. 

</p>
	
	
<h2>Our developers used the following guidelines in creating this methodology:</h2>
	
	<ul>
	
	<li> They used the swiping format that is so pervasive online. Whether looking for a date, a dog, or new digs, the swipe right to accept and swipe left to reject form factor is very familiar to people, meaning there is a minimal learning curve for respondents.</li>
<li> They used a “wisdom of the crowd” approach, a heavily researched metric for prediction. Instead of asking purchase intent, respondents are asked if they think the concept will be successful in the market, which leads to more predictive responses by divorcing the emotion of the decision from asking purchase intent, which has a more personal emotional component. 
</li>
	
<li> They wanted to create a platform that could be used across the development continuum, from early stage seed ideas to fully developed products.</li>
		
<li> They wanted to create an engaging experience for respondents by using quick, easily digestible stimuli to maintain system 1 reactions.  Photos or high-quality images work best, but text can work as well.  Text should be very brief (a sentence, not a paragraph), to maintain the consumers’ ability to process the information and respond quickly.</li>
		
		
	</ul>

<h2>And thus, SwipeRight™ was created! It is executed in four easy </h2>

	<ol>
  <li>  Establish a cognitive baseline by showing respondents directional images and measuring how quickly they swipe right or left. 
 <li> Expose respondents to the stimuli and ask whether respondents believe the concept will be successful (swipe right) or not (swipe left). Respondents evaluate stimuli in a sequential monadic pattern.</li>
	
 <li>	Measure emotional association created by having respondents select which of seven basic human emotions they most associate with the stimuli. The developers chose emojis to represent emotions because they are neutral and therefore non-biasing, and they minimize respondents’ tendency to switch to rational or system 2 thinking. </li>
  <li>	Measure the time respondents takes to select the emotion to signify the intensity of emotional association. </li>
    
 </ol>
	
	
<p>Data captured from these four steps gets modeled to determine the potential for in-market success of each stimuli tested. Including benchmarks (existing concepts with actual sales data) can make the model  even more predictive of actual in-market performance. </p>
<p>Even the first test proved to that the team had built something great. Respondents were asked to evaluate the probable success of ten cars and trucks, using either pictures or high-quality renderings. The average respondent completed all ten evaluations in two minutes. Most importantly, analysis proved the results were highly correlated with actual vehicle sales (as demonstrated through market share). </p>
<h2>What sets SwipeRight™ apart?</h2>
<p>We include prediction models, implicit modeling, and emotional associations and intensity. We’ve discovered that some categories have high levels of implicit reaction - often involving more intense, visceral emotional responses - while other categories are more rational in nature.  Whether the consumer approach is to think-do-feel or to feel-think-do, the technique is equipped to tease that out and model the opportunity accordingly.</p>
	
<p>Over time, Research America has used SwipeRight™ to evaluate many different products and services with a variety of audiences. Marketers love the speed and simplicity of the methodology. Researchers love its predictive capability and how it easily integrates with existing methodologies. </p>
