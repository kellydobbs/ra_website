<h1>Communications: “Marketing is enthusiasm transferred to the customer.” – Gregory Ciotti</h1>

	<p>This is the third post in our series on Customer-Centricity. To be customer-centric and build incredible experiences, we need to fully embrace every detail and fight for each detail to match the experience we want to deliver. </p>

<p>We talked about how this is critical in identifying opportunity and innovation. Now, let’s look at how we can (and should) bring this philosophy to life in our communications.</p>

<p>The best communication speaks to the consumer and connects them to your brand. Is your marketing and communications consumer-centric? Does it exemplify your brand’s passion? </p>

<p>In this technologically advanced world we now live in, consumers are constantly inundated with information. They ‘re forced to filter through information from many channels to choose what is most important. What’s worth paying attention to, and what’s not?</p>

<p>It is our job, as marketers, to find a way to stand out so they pay attention to the problem we solve in their life. Communication that stands out is communication that is created with the customer in mind. </p>

<br>
  <h2><strong>What is the best way to do this? Let’s discuss four characteristics of customer-centric communication:
  </strong></h2>
 
   <p>   1.     Communication must be <b> targeted</b>.</p>


  <p>It’s important to put in the work to identify the correct target for your brand/product. Communication is only effective if it is reaching the correct people. Everything is not relevant to everyone. So do your homework, and define your target market so you can target your message. It’s equally important to identify where your target should be reached. For example, we know that Millennials and Gen Z-ers are the most hyperconnected generations, so it’s important to put social and mobile-friendly communication ahead of traditional media. Go where your target is.</p>


    
 <p>2. Communication must <b>show the value</b> of your brand and product.</p>
  
<p>Customers do not buy your brand; they buy the solution to their problem. Let’s take Simon Sinek’s Golden Circle to illustrate this idea. 
	-	Yes, customers need to know the what of your brand – What are you selling? What do you offer?
	-	And yes, they also need to know the how. How does this product work? How does it meet a need? 
	-	But, what is truly important, is your why. Why does your brand matter? Why does your product make a difference? Why will it make your customers’ lives better?</p>
	<p>
Anchor your message in the why by putting the customers’ needs and pain points first. Not only will customers be given a reason to believe in your product, but they will also be given a reason to buy it. 

	<p>	3. Communication must be <b>omni-channel</b>. </p>
<p>
Meet your customers where they are and communicate through the entire journey at every touchpoint. Customers should feel connected to your brand every step of the way whether they are researching, buying, or reviewing. Additionally, each touchpoint should be on-brand, so the customer receives a cohesive and consistent experience.
    </p>

<p> 4.	Communication must <b>tell a story and be visual</b>.

</p>
<p>Finally, to create customer-centric communication, it must tell a story where the customer is the hero and your brand is the guide. Stories are memorable, and customers are much more likely to see themselves in a story than they are in a list of product features. If you have targeted the right audience and anchored your message in the why, then customers will build a connection to your brand and product.  </p>

<p>If we look closely at the communications from Forbes’ recently released list of the 100 Most Customer-Centric companies, we notice that they consistently hit all four of the points above. The customer is thought of in every message, every sentence, and every word. It’s this level of detail that customers subconsciously tune into and, in turn, are magnetized toward working with these brands.</p>

<p>We want that for you and your customers.</p>

