	<h3>Controlled and Uncontrolled Sampling </h3>
	
	<p>Research America consultants design two basic types of studies that involve quotas – controlled and uncontrolled. Controlled sampling introduces restrictions into the sampling method to limit the researcher’s choice of samples. Uncontrolled sampling allows the researcher to choose sample members at will – no limits on group affiliations are necessary. </p>
	
	
	
	<h3> Applying Quota Control </h3>
	
	<p>	Using Research America’s Sample Management Service, Quota Control allows each survey to be set up so that it includes only a certain number of respondents from any particular group. </p>
	
	<p>For example, assume a client wanted a total of 100 completes with 60 of those being registered Republicans and the other 40 being registered Democrats. Quota Control would allow surveys to be conducted with only Republicans once 40 surveys are completed with democrats. </p>

	
	<p>Research America’s software platform applies the controls, so that programmers and interviewing supervisors know immediately when the quota has been met. Having these systems in place keeps research activity flowing, and keeps survey budgets from spilling over -- avoiding contacting members of a group already well-represented in the study. </p>
	
	<p>Research America’s consultants, programmers, and analysts work together to design comprehensive marketing research studies, providing the exact sampling services that help you collect opinions from any group. Quota management is only part of our sampling services that ensure accuracy and quality in data delivery, providing your company with the information you need to inform your marketing plans. </p>

	
