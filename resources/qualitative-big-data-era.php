<h1> The Power of Qualitative in the Big Data Era</h1>

	<p>Everyone is in love with data: Big Data, Little Data, Data Analytics - if it’s quantitative, marketers are willing to fund it. In fact, if you talk to some data zealots (and most newly-minted MBAs), you may come to believe there is no role for qualitative research anymore. 
</p>
	<img src="../_img/qualitative-big-data-era.png" class="focus-group-facilities-1" alt="The Power of Qualitative in the big new era"> 
<p>  Somewhat ironically, this new emphasis on data is exactly why we still need qualitative research. Qualitative research is needed to help companies understand the “Why,” the “So what?” and the “How?” behind all this data. Qualitative research is needed so that marketers and executives can hear what the consumer has to say. And that is something you will never get from a crosstab or a statistical model. As the old saying goes, quant counts the beans and qual tells you which beans to count.</p>

<p>When we talk about qualitative, we’re throwing a big net: online, in-person, hybrid qual quant, and all the various methodologies included in those topics, such as focus groups, in-depth interviews, ethnographies, shop-alongs, IHUTs, and central location usage tests (to name just a few). Additionally, technology, in the form of mobile or online qualitative, has helped overcome two of the major limitations of qualitative research: geographic restrictions and small sample sizes. All of this makes qualitative research a valuable contributor to consumer insight.</p>



<br>
  <h3><strong>So, throw away your notion that qualitative research only comes in the in-person focus-group package, and think about how you can benefit from some of these advantages of qualitative research:

  </strong></h3>
  <ol>
    <li>
      
   	  <strong>Fewer Time Constraints.</strong> Think about it. If your survey takes 10 minutes to complete, that’s all the time you have. On the other hand, qualitative research usually does not have such built-in time restrictions and can last as long as needed (within reason!) to understand the topic. Some qualitative research follows the respondent over weeks or even months to understand how they proceed through a specific task or situation (such as buying a car or a new home). This allows the results to have a certain subtlety to them that is not possible with quantitative research. </li>
	  
	  <li>	<b>More Free-Flowing and Fluid. </b>Quantitative research follows a defined process, especially in data collection. Surveys are programmed to allow the presentation of questions and stimuli in a specific order, whereas qualitative research can adapt to the information that is being gathered. If one approach is ineffective, a good qualitative researcher can immediately shift gears and seek to explore data in a new direction. If the researcher hears a promising thought, they can easily and quickly incorporate it into the discussion. </li>
	  
	  
	<li><strong> 	Qualitative Research data is Human</strong>. Qualitative research allows for respondents to give both logical, reasoned, rational thought, and gut response. Therefore, the information collected during qualitative research can more completely reflect how humans think and behave.</li>
	  
	<li><strong>More Predictive than Historical Data.</strong> As soon as you see a market trend, it’s already too late to respond. Data analysis focuses on the past: the data you are working with has already been collected. Qualitative research, on the other hand, is completely in the moment, and because of its adaptability, is much more likely to generate new-to-the market ideas and insights.</li>
	  
	<li><strong>	Access Emotions. </strong>The open-ended nature of qualitative research makes it possible to get underneath superficial responses to gather information from an individual’s emotional response. As we learn more about how emotions influence brand behavior and purchase behavior, we will find that qualitative research is invaluable to tap into this asset.</li>
<li>	<strong>Save time and Money.</strong> Qualitative research can often be completed quickly and on a limited budget because they typically use smaller sample sizes than other research methods. Online and mobile technologies also contribute to faster project completion. And, if results are obtained faster, the project moves forward faster, which is something we all need.</li>
	<li><strong>More Content for Creatives and Marketing Teams</strong>. Qualitative research is based on an authentic, honest expression of ideas between the researcher and the respondents. This communication can produce important elements that, when incorporated into marketing programs, taps into important rational and emotional responses for the consumer.</li>
	<li><strong>	Real-World Storytelling.</strong> Nothing is more motivating than bringing real consumer stories to the leadership team, especially when accompanied by video. While you can argue that quantitative research tells stories as well, to really change perceptions and galvanize action, there is nothing comparable to hearing it first hand from the customer.</li>
  </ol>
	
	
  
<p>Qualitative research is and will remain an important research technique for those who want to better understand consumers, and at a deeper level. The key to making the best use of qualitative research within a marketing program is to understand the wide variety of qualitative techniques now available, and to be creative in applying them to your research needs. Just as Big Data can’t be the sole answer, qualitative research now goes far beyond focus groups!</p>

