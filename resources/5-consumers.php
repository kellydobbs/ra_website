<h2>	How a rapidly changing landscape affects the way we think about shopper engagement and insights</h2>

<p>When marketers have the direction and confidence to focus their efforts on the moments of maximum influence, they stand a much better chance of reaching consumers in the right place at the right time and with the right message. If they can do that, they have a better chance of influencing shopper behavior. 
</p>

<p>However, in a world where the shopper landscape is changing dramatically, knowing how to adapt that learning is an equally important component of shopper insights work and a key structural element of our approaches. Going forward, marketers must re-align their efforts with the realities of a more complex shopper decision-making process.</p>

<p>Historically, we have viewed the purchase path in a linear fashion (the awareness – interest – consideration action funnel). Marketers used interaction with shoppers at these touchpoints to help them reduce options in their consideration set and emerge from the customer journey with a single brand to purchase. Hence, we deployed research tools along the way based on our understanding of the process.
</p>
	
	<p>That used to be a very effective marketing approach, but shoppers now travel a different path, presenting both challenges and opportunities for marketers. Today’s shopper is on more of a journey, with lots of stops for conversation along the way, and maybe even changes in direction and destination.</p>

	
	
	<p><center>
	  <img src="/_img/5Consumers-Trends.png" width="345" height="234" alt=""/>
	</center></p>
	<p><strong>Today’s shopper has undergone a fundamental transformation in buying behavior, driven by multiple factors
</strong></p>
<ol>
  <li>
 		There is a nearly overwhelming number of product choices.</li>
	  
  <li>	Digital media results in an explosion of information.
</li>
	  
  <li> The average consumer has a higher level of buying sophistication than ever before and understands marketing messages in relative to other information sources.</li>
	
   <li>	Consumers are living with continually increasing pressure on their time and financial resources.
</li>
    
	<li>	There is wide proliferation of alternative distribution channels for consumers to access.</li>

	
	
	
  <h3>  Shopper insight professionals must, therefore, change their approach to win in this brave new world. Here’s how:</h3>
    
 <p><b>It’s not the winnowing process we initially thought</b>. Instead of narrowing down brands in the active consideration set, consumers typically expand the list as they shop the category.</p>
	
<p>Shopper Insights Opportunity: All touchpoints are not equally impactful, and marketers must identify which touchpoints make a difference, how that works, and where to insert marketing efforts.</p>
	
<p><b>It's no longer a one-way conversation where the brand message is pushed to the consumer</b>. Today, nearly two-thirds of consideration phase touchpoints involve consumer-initiated activities such as internet reviews, word of mouth recommendations, and in-store interactions. </p>
	
<p>Shopper Insights Opportunity: Marketers must identify and optimize digital properties such as informational web sites, programs to foster word of mouth recommendations, that influence these all-important consumer-driven touchpoints.</p>
<p><b>The store (either online or brick-and-mortar) is more than a distribution point.</b> Today, more consumers hold off on their purchase decision until they are actually at the point of purchase. And even then, up to 40% change their minds based on something they see, learn, or experience at that point. Also, consumers with dwindling home space are no longer stock-up shopping. Rather they are doing more grab and go shopping, relying on extreme couponing for savings. </p>
	
<p>Net, winning the point-of-purchase battle is critical and requires a great combination of packaging, shelf position, fixtures and signage for retail and page position, content, and positive reviews online.  </p>
<p><em>Shopper Insights Opportunity:</em> Identifying the ideal mix and composition of these touchpoints, messages, and experiences at the point-of-purchase will be critical to getting the shopper to purchase.</p>
	
<p><strong>Time compression is squeezing all of us</strong>. We all have more demands that occupy our days, and as a result, we have less control of our time and are forced to do more with less. Today, 70% of moms with kids under 18 work, and only about 20% of parents are “stay-at-home” so marketers are competing for the high-valued shopper purse while knowing that they don’t have as much time to browse.</p>
	
<p><em>Shopper Insights Opportunity:</em> Marketers must understand the path to purchase and determine how to simplify the shopping process from beginning to end to win time-stressed consumers.</p>
	
<p>Fine-tuning your understanding of your shopper’s journey is critical to being able to influence the purchase decision. As we stated above, it has changed dramatically. But guess what? It’s going to continue to evolve, so you can’t stand still! Shopper insight research will continue to play a critical role in understanding how shoppers behave today. Make sure your shopper insights projects, such as Decision Hierarchy, Path to Purchase, Virtual Store Testing, and Journey Mapping, are taking full advantage of consumer shopping trends.</p>

