	<h1>Research America Now Provides Health & Wellness Consumer Insights and Trends</h1>

	
    
<p>
	  <strong>Newtown Square, PA, A few months ago</strong>, Research America Inc. merged the<a href="NMI-Research-america-merge.php"> Natural Marketing Institute</a> into their family of companies. This acquisition expanded Research America’s capabilities to include additional strategic consulting services, proprietary custom methodologies and syndicated databases that include over 1 million consumer interviews in over 30 countries, trended for more than 20 years.  </p>
	
<p>Among the expanded capabilities Research America now provides is the Institute&rsquo;s annual Health &amp; Wellness in America, Consumer Insights and Trends Report.&nbsp; This report, now in its 20</span>th year, provides insights into how today&rsquo;s consumer is confronting their own health and wellness, the health challenges they encounter and how their behaviors and attitudes have transformed, adjusted, and realigned over the past decade in response to the ever-changing and uncertain world.</p>
	
<p>The content of the report includes the following: segment profiles highlighting the mainstreaming of health and wellness and the emergence of new health leaders, the growing connection between convenience and health, the proliferation and trial of plant-based offerings, the emerging understanding of the gut-brain connection, the increased importance of values-based attributes driving purchase decisions and much more.</p>
<p dir="ltr" id="docs-internal-guid-86a9d965-7fff-82aa-7767-aded503572f4">Integrating this consumer intelligence helps businesses identify opportunities for new and existing brands, products, and categories. Being able to better understand the five distinct consumer segments, their loyalties, and behaviors, businesses are able to maximize their strategies and increase their return on investment.&nbsp;<br>
</p>
<p dir="ltr" id="docs-internal-guid-4f6c4035-7fff-e0a6-43d2-d173f477b33f">Robert Porter, Research America CEO, said &ldquo;<em>Over 100 million consumers, which is about half of American adults, state that due to COVID-19 they are more worried about their personal health.&nbsp; However, recent research from the Institute indicates a significant difference in what consumers say versus what they do because 37% of consumers state they&rsquo;re eating more junk food these days. Being aware of these inconsistencies is critical for businesses, especially when they&rsquo;re considering new product development or creating effective marketing strategies.</em>&quot;</p>
<p>Research America has grown to 14 offices nationally and more than 300 researchers.&nbsp; The company provides a full-service suite of capabilities including consumer web panels; quantitative data collection; qualitative facilities; sensory and user experience lab testing; and data science capabilities.</p>
<p>In 2019, the company was named one of the Top 50 market research firms in the United States based on annual revenue by the GreenBook Research Industry Trends Report (GRIT). Research America has also been named by Inc. magazine as one of the fastest-growing privately held companies in America. The company has grown organically and through acquisitions of boutique research firms in specialized verticals.&nbsp; For more information on the proprietary Health &amp; Wellness data or full-service market research capabilities, please call 610-356-1800 or visit <a href="https://researchamericainc.com/">www.ResearchAmericaInc.com</a>.</p>

