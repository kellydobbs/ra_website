	<h2 class="big-link"> RAI Case Study #3</h2>
	
    <h1>Topic: Revitalizing a Mature Brand</h1>	

		<p><img src="../_img/RAI-case-study-31.jpg" alt="Market Research - Revitalizing a Mature Brand" width="397" height="147" />
			
	<h2>The Client</h2>		
 <p> The advertising agency for a mature cleaning category brand whose household penetration had slipped 5.5 points over the previous three years. 
 </p>

<h2>The Problem</h2>	
	
	<p>  Many mature brands create a difficult challenge for advertisers and their agencies. In the eyes of the consumer, mature brands are often viewed as being a bit old-fashioned and thus become difficult for clients and their agency partners to create fresh relevance. Consumers believe they already know the brand and what it stands for and often resist changing well entrenched attitudes. 
This brand was losing top-of-mind-awareness as the category leader outspent it four-to-one. Market share was significantly declining due to lack of differentiation and relevance. <a href="https://researchamericainc.com/consumer-insights.php">Consumer insights</a> were needed to uncover why the brand had become less relevant and how to reverse the trend.

</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>The brand’s advertising agency was called upon to develop a new campaign that would re-position it in the minds and hearts of consumers. The agency turned to us to uncover the functional and emotional insights that motivate people to choose specific brands in this category. They also needed a richer, deeper understanding of strengths and weaknesses of the brand.</p>
	
<p>In order to achieve the objectives of the research, we surveyed consumers in two markets – one with a high brand development index and one with a lower one (this allowed for both brand loyalists and brand switchers to be heard from and measured). We conducted the research using a central location testing methodology. This approach, which provides instantaneous analysis of consumer response, allowed the research team to turn large amounts of data into relevant insights, quickly. These insights were further developed, and then validated as the session progressed.
</p>
	
	
<p>	
Given the real-time presentation of consumer insights, an exciting creative momentum developed among the brand team. Ideas that did not resonate as strongly with consumers were quickly eliminated and winners were built upon. The final winner was a synthesis of various elements of consumer beliefs, benefit statements, and RTBs.

</p>

<h2> The Outcome</h2>
<ul>
<li> <strong>Result 1:</strong> There was a dramatic increase in the client brand’s baseline volume during the advertised period, with growth of 15.1% versus a decline of -3.3% the year before.</li>
		
<li><strong>	Result 2:</strong> The brand’s rate of ‘shelf turn’ increased by +14.2% during the advertised period. Which was 2.5 times higher than the leading competitor (on a quarter of the ad spending). It was also the highest of any brand in the category.</li>
<li><strong>	Result 3:</strong> With only two months of advertising, the client brand’s top-of-mind awareness jumped four points, reversing the 3-year decline.</li>
<li><strong>	Result 4:</strong> The campaign received an incredible amount of media attention. PR impressions alone amounted to value worth approximately 13% of the print budget. </li>


	</ul>
	
	
	
