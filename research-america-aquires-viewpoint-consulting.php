	<h1>Research America, Inc. Acquires Viewpoint Consulting</h1>

	

	

	<p><b>PHILADELPHIA - July 19, 2016 </b> - PRLog -- Research America, a nationwide provider of marketing research, qualitative and quantitative surveys and analysis, announces the acquisition of Viewpoint Consulting, a leading agency specializing in Pharmaceutical Payer Research to augment its existing pharmaceutical offerings.</p>
	
	<p>
	  Headquartered in Langhorne, PA, Viewpoint Consulting offers a diverse and comprehensive portfolio of marketing research services to pharmaceutical and healthcare companies throughout the United States. Viewpoint Consulting's team members have played roles in valuable healthcare payer research conducted by some of the largest, global pharmaceutical and healthcare companies. The acquired firm was founded in 1998. </p>
		
	<p>  &quot;This is a tremendous opportunity for our staff to realize new projects and growth opportunities. Expanding the services offered to our existing medical clients, allows us to compete for projects on a much larger-scale,&quot; said Robert Porter, President and CEO of Research America.</p>
		
	  <p>Viewpoint Consulting has provided its clients insight into the dynamics of the pharmaceutical and biotech industries, focusing on product marketing to &quot;concentrated buyers&quot; within Managed Markets. Managed Care Market Research is at the core of Viewpoint Consulting's strength. The organization conducts primary research through advisory boards, focus groups, and one-on-one and telephone in-depth interviews.</p>
	
     <p> Viewpoint Consulting's founder Norm Smith and his research team will continue to support their clients from their current office location. Mr. Smith has been named Director of Payer Research in the new alliance.</p>
	  <p>
	    This transaction marks Research America's fourth successful acquisition during the past year.
	    Through its purchase of Viewpoint Consulting, Research America will continue to develop Viewpoint Consulting's unique relationships with pharmaceutical companies. The goals of payer research involve helping clients build effective access programs. This will allow them to develop &quot;defensible value propositions&quot; and help define price points which the market will tolerate.</p>
	  <p>
      Research America continues to expand its network of local marketing research centers by evaluating opportunities to open local, branded offices throughout the United States. The firm has established a network of marketing research facilities based in its headquarters in Newtown Square, Pennsylvania. The company is in the process of expanding its pharmaceutical and healthcare marketing research division.</p>
	
	  <p>
	    Research America operates 11 additional marketing research facilities throughout the country.  It offers energy and utility companies dedicated marketing research specialty expertise through Metrix Matrix, located in Rochester New York. The company owns a central testing location facility in Totowa, New Jersey.  Its medical/pharmaceutical offices are located in Newtown Square Rochester, Langhorne and Willow Grove.
      </p>
     
	  

	

