	<h2>Background</h2>		
 <p>Progressive Mexico-based marketer of health and beauty products wanted to develop strategies to accelerate its growth in the U.S. Client needed to gain a richer, deeper understanding of where its brands live in the mind of the U.S. Hispanic consumer and identify core drivers of purchase by category. The overall objective of this research is to understand – with high levels of confidence – what drives purchase intent within the respective categories and assess how the client’s brands, and those of competitors, are performing against these drivers.  
 </p>




<h2>Approach</h2>	
	
	<p>  Quantitative survey conducted among a representative sample of 500 US Hispanics who have purchased OTC remedies and beauty care products in the P6M. 

</p>

	
	<h2>Results</h2>
	
	<p>BBN, a form of graphical modeling, was utilized to determine the drivers of purchase interest. A composite measure of Brand Equity was derived from brand associations across all attributes (performance), weighted by category drivers’ importance. Brand ‘funnels’ and conversion metrics were provided for each category. Quadrant maps and perceptual maps were also produced for each category. </p>


  
