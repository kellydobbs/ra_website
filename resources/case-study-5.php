<h2>The Problem</h2>	
	
	<p>   The client wanted to evaluate and optimize multiple conceptual positionings for their new breakfast items. In addition, they wanted to explore consumer reaction to multiple versions of in-store and drive-thru menu displays, as well as understand consumer reaction to a variety of in-store POS materials and direct mail pieces. Finally, they wanted to find an ideal name for particular products within the menu lineup that fit with the company’s brand.

</p>

	
	<h2>Our Recommended Solution</h2>
	
	<p>A proprietary blended in-person quant-qual methodology was deployed. This approach allowed the team to confidently understand the relative strengths and weaknesses of each positioning in real-time, providing the ability to optimize and re-test ‘in the moment.’ </p>

		
	

<h2> The Outcome </h2>
<p>	The client launched a new breakfast product line-up using the most appealing product platform identified in the <a href="https://researchamericainc.com/market-research-company.php">market research</a>. The launch was accompanied by new menu display boards and new in-store signage and marketing collateral. Breakfast sales increased 13% in the year following introduction of the expanded breakfast offerings.
  </p>
