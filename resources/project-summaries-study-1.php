	<strong>Project</strong>: Arena Suite Holders - Customer experience & loyalty measurement</p>
		
		
		
	<h2>Background</h2>		
 <p>As a primary “customer” segment of an arena, Suite Holders represent both a source of strong revenue support as well as an indicator of future growth opportunity. With multiple venue options available today, investment by Suite Holders in a particular venue is a valued asset not to be taken lightly. The arena wanted to ensure that it was maximizing the experience for Executive Suite Holders and retaining loyalty. To gather insight and provide greater understanding of their needs, obtaining their direct feedback in a nondisruptive but insightful way would be highly beneficial. 
</p>




<h2>Approach</h2>	
	
	<p>  Executive Suite Holders were invited to participate in an on-line customized survey designed and hosted by RAI. Over a five-day period, more than 40% of the Executive Suite Holders responded with their thoughts, their ratings of the arena in several areas and verbatim comments regarding potential enhancements.

</p>

	
	<h2>Results</h2>
	
	<p>Through the survey, the arena owners were able to:		
</p>

    <ol>
      <li>		Identify those issues which were extremely important to the satisfaction and renewal decisions of Suite Holders.
</li>
		
	 <li>			Establish benchmark performance ratings in those key areas. Having a “picture” of what was important to Suite Holders and their current perceived performance allowed arena management to focus their marketing messages, address key issues motivating renewal or Suite upgrade, choose optimum media vehicles and prioritize investments in key benefit and service areas shown to enhance satisfaction. 

</li>	
		
    </ol>
	
