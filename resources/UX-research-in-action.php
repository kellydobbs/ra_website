<h1>UX Research in Action – Some of Research America's Case Studies</h1>

	<p class="med-link">Case Study #1 - Understanding Less Engaged Consumers </p>
	

	
	<p><strong>Background:<br>
	</strong>		A popular video sharing platform wanted to attract more users. Specifically, they needed to understand the user experience obstacles of people over the age of 25 and previous (“churned”) users who had stopped frequenting the platform. Additionally, the platform wanted to explore the perceptions of their app against perceptions of other similar apps available in the market.</p> 


<p><strong>Approach:<br>
</strong>	Research America executed a three-phased approach using both qualitative and quantitative methodologies:
	</p>
<ol>
  <li> 
  In-depth interviews with 300 monthly active users and 300 churned users in the U.S. 
    
   
  <li>  Exploration focus groups in three diverse U.S. markets with participants that were monthly or daily active users, churned users, and people who had never used the app. The participants engaged with the app as a group reviewing content, sharing their use experience when showing features to churned and non-users, and discussing perceptions of competitor apps.    
  </li> 

  <li>  A quantitative UX App Test with thirty non-users downloaded the app and engaged with it daily over one-week, providing feedback on their experience.
       
  </li>
</ol>
	
   
    
 
	

<p><strong>Results:</strong><br>
This research was completed sequentially, so that each phase of the research informed the subsequent phases. The combination of qualitative and quantitative research deepened our client’s understanding about what obstacles to engagement exist for non-user populations. The insights gained in the research are currently being used to develop marketing strategies and improve the interface for a better user experience.  </p><br>

<p class="med-link">Case Study #2 - Aligning Website with Audiences</p>


	
	
	<p><strong>Background:</strong><br>
    A non-profit, community health care system was experiencing shrinking brand equity scores in their annual tracking research as a result of demographic changes in their community. They wanted to understand their audiences’ current needs to facilitate a website re-design that would deliver a better user experience.</p>
	<p><strong>Approach:</strong><br>
    update line under Approach: Research America's Mobile UX Lab was utilized to complete specific tasks and observe the user’s natural exploration (without guidance) of the website</p>
	<p><strong>Results:</strong><br>
	  The resulting persona development enabled us to understand the motivators behind the level of engagement individuals have with their health and wellness, and their subsequent relationship with the healthcare network and website. By understanding the established consumers’ minimum expectations for the website, we were able to uncover game-changing opportunities for the brand and its relationship with the community. These insights were a strong foundation for defining the role the website should and can play with various audiences in the community which led to defining a strategy for the re-design.<br>
    </p>
	<br>
<p class="med-link">Case Study #3 - Innovating the Connected Living Room</p>
	
	
	<p><strong>Background:</strong><br>
    Our client wanted to define the future of the connected living room and generate viable product ideas to develop for market introduction within six months.</p>
	
	<p><strong>Approach:</strong><br>
    The research consisted of a series of consumer immersions, followed by an insights-to-innovation workshop. Online video diaries and in-person at-home interviews documented how people were connecting devices throughout their home, and particularly within their living room and TV. The resulting consumer videos and insights inspired a two-day innovation workshop using rapid prototyping and testing ideas in-the-moment with consumers.</p>
	
	<p><strong>Results:</strong><br>
	  The research generated more than 50 new product ideas, and three products were selected to present to leadership, using video from the study to demonstrate potential market interest and value. The client launched a new product into the market within six months of completing the project.
    </p>
	
