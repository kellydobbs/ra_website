	<h2>Background</h2>		
 <p>Most National Dental Practice-Based Research Network surveys are offered online. Online surveys have dominated the landscape when gathering insight. However, achieving increased response rates, while decreasing a perceived bias proved challenging. Researchers must ensure that there are no biases with online surveys that might overestimate key dental practice characteristics when older dentists might lean towards not completing an online survey. There is also a need to increase response rates.

</p>




<h2>Approach and Results</h2>	
	
	<p> RAI implemented a multi-mode approach. The paper survey was designed to have the same look and feel as that of the online survey. RAI reviewed every survey question to avoid any biases within the language and wording. RAI was also able deploy the survey via mobile devices to provide additional platforms to complete the survey. Several of the recommendations by RAI were to send multiple forms of reminders about the importance of completing the survey. The thorough the multi-mode approach, response rates increased.

</p>

	
