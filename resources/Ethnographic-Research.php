<h1>How to Use Ethnographic Research for Innovation and Strategy</h1>
<img src="../_img/ehtnography.png" alt="ehtnography" width="400" height="248"> <p>According to the Insights Association, “Ethnography involves the study of a small group of subjects in their own environment. … The ethnographer attempts to get a detailed understanding of the circumstances of the few subjects being studied. Ethnographic accounts, then, are both descriptive and interpretive; descriptive, because detail is so crucial, and interpretive, because the ethnographer must determine the significance of what she observes without gathering broad, statistical information.”<br><br>
  Ethnographic research participants are recruited to allow the interviewer into their environment, where the interviewer can observe the participant engage in the behavior in question. That might be at home for testing a product, an information search, or a decision. It might be the office or job site for a B2B ethnography. Or it might be a restaurant or hotel, depending on the research topic. The key to ethnography is that the interviewer joins the participant in the participant’s environment. <br><br>
  A second important key to ethnography is that it emphasizes observing behavior or social phenomena, rather than the focus of testing hypotheses in traditional marketing research. The interviewer wants to watch the participant complete a task, whether that means baking a cake from a mix or using a power tool on a construction site. The interviewer will ask clarifying questions to explore and understand why the participant made specific choices in completing the tasks. Interviews are usually audio-recorded but may also include video recording. <br><br>
  In ethnography, the researcher gathers what is available, what is normal, what it is that people do, what they say, and how they work. Then, the researcher looks for patterns in the results of the interviews, how ideas and beliefs through language, and how they behave as expressed through the actions that the interviewer observed.<br><br>
  There are several challenges to ethnographic research. First and foremost, the interviewer must be both participant and observer in the setting with the participant. Highly-trained ethnographic researchers are needed to walk this fine line without introducing a personal bias into the results. Ethnographic research can take longer than traditional marketing research qualitative work, which may be an obstacle for some projects. Because participants may be hesitant to invite interviewers into their environment, it may take longer to recruit for ethnographies. And finally, you may be limited geographically in where you can recruit participants and incentive costs will probably be higher than with other research methods.<br><br>
  Despite the drawbacks, ethnographic research has been growing in popularity, especially in research for innovation and strategy, because it can provide uniquely powerful insights.</p>

	
