	<h1>A Proven Approach to Market Segmentation </h1>	

	
    <img src="../_img/segmentation-illustration.png"  class="focus-group" alt="market research segmentation">  
<p> Market segmentation is the ability to identify high-opportunity customer groups within the marketplace, and distinguish them from marginally profitable customers. Segmentation is based on the premise that no market is homogeneous in terms of what consumers need or want from products and services. <a href="https://researchamericainc.com/services/market-segmentation.php">Market segmentation</a> is becoming more popular as a method to develop business strategies because of the information and competitive advantage it provides. Segmentation and micro-targeting replaces a mass-marketing strategy to more effectively identify key customer groups and reach them more efficiently with product offerings, pricing strategies, product information and promotions targeted to very specific targets within the customer and prospect audiences. </p>
 
 
	<p> Market segmentation allows businesses to:</p>
	
	<ul class="check">
	<li> 	Design products and services and price them based on the wants and needs of the customer.
	</li>
	<li>	Focus attention and resources on the most compelling market opportunities.
	
   <li>Develop targeted marketing and communications tactics and campaigns based on the attitudes and expectations of prospects.</li>
		
	<li>Develop customized value proposition and messaging platforms.</li>	
		
		
    </ul>
	
	


<p>Segmentation allows scarce marketing resources to be directed at customers and potential customers in ways that are most likely to develop them as loyal customers. The information from a segmentation study allows the needs and interests of various market groups to be identified and individually addressed; product enhancements or new products can be assessed in terms of their appeal to targeted groups; and marketing-communications can be directed to an audience with an affinity for the message.</p>

<p>The objective of market segmentation is to identify groups of individuals who are similar in terms of attitudes, behaviors, demographic, or firmographic characteristics. The goal is to develop clusters of individuals who are similar while maximizing the dissimilarity between individuals in different clusters. Once the segments are identified, they are profiled on other key variables to further understand their characteristics. After all segments are fully developed, a marketing strategy can be designed to target and communicate with the groups that are most essential for maintaining or growing the business. Because members of each segment have similar interests, attitudes and behaviors, they are likely to respond similarly to elements of the marketing mix.</p>


<p>Market segments are formed using multivariate statistical techniques such as cluster analysis to group like-minded customers (or businesses) and prospects and differentiate them from others in the marketplace that possess other values and interests. The statistical application explores the data and creates these marketplace groups based on the characteristics they possess.</p>

<p>Segmentation does not mean that one critical group is the sole target while the others are excluded; instead, unique messages, promotions, prices, and product enhancements are developed to appeal to the known differentiating needs of each segment.</p>

<p>These segments can be developed from many variables that explain and predict behavior; customers and prospects may be segmented based on:</p>
	
	<ul class="check">
	  <li>	Attitudes</li>
	<li>	Behaviors</li>
	<li>	Buying and usage habits</li>
	<li>	Demographics or firmographics (a priori segmentation)
<li>	Psychographics</li>
<li>	Or a blended approach</li>
<li>	Existing customer data, sourced from the client can also be included </li></ul>

<p>As with all of our work, we believe a market segmentation study must produce meaningful and actionable results to our clients. To achieve this, the correct research design and analytics must be chosen based on the objectives of the client and customized to meet the client’s specific needs.  We do not use a “black box,” one size fits all approach to the design and analysis of market segmentation studies.  </p> 

<p>Although each study is unique, most will follow this progression:</p>
<ul class="check">
	<li>	Assist client in problem definition and determine if a segmentation study is appropriate to meet their informational needs
	  <li>	Establish research objectives</li>
		<li>	Review previous research and corporate data resources</li>
		<li>	Identify metric measures</li>
		<li>	Develop survey and analytical methodology after reviewing options with the client</li>
	<li> Conduct data collection</li>
	<li>	Analyze data and create models</li>
		<li>	Create, define and refine segments that are practical in their description of the marketplace, actionable for the client, and measureable </li>
	<li>Consult with client (as needed) to identify targets, customize the approach, and execute strategy</li>
</ul>
<p>The number of segments identified through the research is determined by the data analysis, but must be a manageable number. It is important that the groups be large enough to be meaningful and that their composition be actionable.</p>
