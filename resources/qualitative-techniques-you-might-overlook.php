<h1>Beyond Focus Groups: Qualitative Techniques You Might be Overlooking</h1>

	<p>I say qualitative, you say focus group. </p>

<p>It happens all the time. Marketers’ knee-jerk response to qualitative research is to run focus groups. </p>

<p>Sometimes focus groups are the default qualitative methodology because marketers want “more” from the research by way of number of participants. However, in qualitative research, we get “more” with quality. In qualitative research (and marketing research generally), quality is delivered when you have the right respondents, evaluating something in the right context or moment, with the right questions. Qualitative research must be designed to extract the best information for the precise moment we are trying to influence or for which we are building solutions.</p>

<p>While the focus group is one very handy and valid approach, qualitative research has a wide variety of creative and valuable techniques that might better meet your needs:</p>
<ul>
<li><b>	iHUTs, or in-home usage tests,</b> are incredibly useful to observe how consumers use products in their real-world settings. Whether you want to see how home chefs use your product in preparing a meal, how families consume entertainment media, or how DIYers renovate their homes, iHUTs are a terrific tool.</li>
	
	<li><b>	IDIs, or in-depth interviews</b>, allow marketers to explore topics deeply with qualified respondents. Whether these are done over the phone, in person, or via webcam, they give critical insight into consumer motivations and perceptions. IDIs are especially useful when you are talking about sensitive topics with consumers, such as health or finances. Additionally, IDIs are a very effective way to understand the perceptions of business people who might not be willing to share openly in a group setting with potential competitors.</li>
	<li>	<b>Dyads and Triads</b>, sometimes called mini-groups, are essentially discussions involving two (dyad) or three (triad) respondents simultaneously. These groups can be very helpful for explaining how consumers make decisions when there is more than one party involved, such as when a couple purchases a home, or a family decides where to go on vacation. Similarly, the same holds true for B2B decision makers, where hierarchical roles may influence sharing on potential sensitive or divisive topics.</li>
<li><b>	Online qual boards</b>, or immersion board sessions, are asynchronous moderated online discussions, comparable to an in-person focus group. A moderator posts questions or assigns tasks, and then follows each participant’s online activity and probes to clarify responses. Participants complete required activities (e.g., video diaries, picture collages, image sorts, journey mapping, storytelling, etc.), can comment on other participant answers and activities, and respond to follow-up questions from the moderator. These exercises allow the moderator to connect with respondents on a deeper, visual, more emotional human level and help to illuminate similarities and differences amongst respondents and segments. </li>
	<li>	<b>Shop-Alongs</b>, require recruiting qualified respondents to shop for a specific product, while being accompanied by an experienced interviewer. Understanding how consumers react in-store - with all of the POP materials and competitive product selections available - can be revealing for marketers in creating a retail consumer experience.</li>
	<li>	<b>UX research</b>, or user experience research, is often used to evaluate interfaces and usability for websites, product packaging, software and apps. In this technique, qualified respondents are often recruited and asked to perform a series of tasks to understand how easily they interact with the topic website.</li>
	<li>	<b>Ethnographies, </b> are used to explore decision making patterns, including information search, product comparison, and purchase. For example, the City of Atlanta wanted to understand how families chose Atlanta as a weekend travel destination, how they decided where to stay and what restaurants to patronize, what activities they research and eventually chose, and who made the final destination. Another ethnographic study looked at how construction workers used a particular power tool and found ways to improve the tool and worker training to increase productivity while make using the tool safer.</li>
</ul>
<h2> Considerations </h2>

<p>One of the great benefits of all of these techniques, as with focus groups, is the ability to video the research process, and then to use video to illustrate the research findings. A well-edited video can quickly summarize and reinforce the results of the qualitative project. </p>

<p>While these techniques are valuable for many research questions, they do have some additional challenges for the researcher. In addition to the usual concerns about high-quality recruiting, personal security, and professional respondents, researchers should have experienced in handling test products to ensure safety and quality. After all, if you are shipping test products to respondents, you want to be careful in the precautions you take to preserve product quality and confidentiality. The logistics of these qualitative techniques may be more complicated than those of a focus group.</p>

<p>Additionally, not all researchers are trained and experienced in conducting interviews and tests with individuals or small groups of consumers.  Just because a moderator is outstanding at conducting focus groups doesn’t automatically mean they are qualified for or experienced with other types of qualitative approaches. Each methodology requires a unique set of skills and experiences. </p>

<p>The most critical requirement for successfully using these qualitative research techniques is to find research partners who have the expertise, training, and experience to conduct them appropriately. Not all qualitative researchers are the same, so be sure that you have a team that is well-versed in the specific technique you are using.</p>

