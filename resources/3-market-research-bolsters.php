<h1>3 Ways Market Research Can Bolster Your Marketing Creative</h1>

	<p>Market research plays an important role in gaining a better understanding of your company, what motivates your target audience and trends in your industry. Did you know it can also be vital to your advertising efforts? We can get even more mileage out of market research when it is used to help direct creative efforts. By understanding your customers’ experiences, competitors, and industry trends, market research can help guide the design and content of your marketing and advertising. The following are three ways to use market research to inform your advertising strategy and bolster your marketing creative. </p>
	<p><span class="med-link-2">Amplify Emotional and Rational Connections to the Brand</span><br>
	  We can get to the heart of the emotional and rational connections to your brand to improve your marketing creative. For <a href="case-study-2.php">one of our projects at Research America</a>, we used an online qualitative discussion to understand these connections, as well as the motivations to purchase and pain points experienced. Once the data was collected, we were able to make several recommendations to enhance the marketing creative. This included portraying friends having a good time in advertising and emphasizing the sense of nostalgia and childhood memories the brand elicited. </p>
	<p><span class="med-link-2">Ensure All Components of Your Marketing Creative Are Working Together</span><br>
	  Market research helps assess the effectiveness of your advertisements, but can also analyze the impact of visuals, characters, copy, taglines, and the main message to confirm that your target audience is receptive to the creative. Research America <a href="case-study-4.php">performed this analysis for a major drug manufacturer</a> by deploying a multi-phase research program. It combined exploratory qualitative research and a central location test blending quantitative and qualitative research. As a result, the research confirmed that the chosen print ad was engaging and propelled the audience to seek additional information. Also, the research identified the patient segment most receptive to the advertisement’s message. In the year after the launch, sales of the prescription drug increased 35%. </p>
	<p><span class="med-link-2">Communicate the Right Key Message in Your Marketing Creative</span><br>
	  Your key message needs to do more than drive appeal. The message needs to win over your target audience and differentiate your brand among competitive messaging from other brands. When a pharmaceutical company needed to jumpstart lagging sales of a new prescription drug, we <a href="case-study-6.php">implemented a multi-step research plan </a>that blended both quantitative and qualitative research to develop the best positioning statement for the brand campaign. </p>
	<p>Market research also can be used to determine your “winning” campaign. A manufacturer asked us to <a href="project-summaries-Study-6.php">evaluate advertising for a heartburn medication </a>and we used a comprehensive communications testing approach to narrow down the ads that were most successful at communicating “life without heartburn.”</p>
	
