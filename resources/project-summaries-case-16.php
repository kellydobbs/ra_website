	<h2>Background</h2>		
 <p>This Fortune 500 B2B client commissioned a brand equity and assessment study. The clientâ€™s objective was to develop a new, forward-looking brand position that is relevant and meaningful to employees, customers, and prospects. To accomplish this, the client team first wanted to understand their brand promise of value, strengths, weaknesses and opportunities; and the perceptions and associations that key audiences have of the brand. 
 </p>




<h2>Approach</h2>	
	
	<p> Qualitative research conducted previously for the client was used to design an international brand strength study. The questionnaire was translated to native language and programmed for data collection. The survey was then executed online with purchase decision makers and influencers. The research analyzed 30 brand attributes for the client and competitor brands. Quotas were established by global region and by industry category, and data collection was completed during a three-week period in 9 countries. 


</p>

	
	<h2>Results</h2>
	
	<p>Research results produced brand strength and equity scores for the client brand and three competitive brands. Drivers of brand strength were identified through multivariate metrics, including factor and multiple regression analysis. The analysis identified the current position of the client brand relative to competitive brands. Selected brands were assessed on the potential for future positioning. The client was able to develop a new, forward-looking brand positioning consistent with how the brand is viewed today and what the brand aspires to be in the future.
</p>

	
	
	
