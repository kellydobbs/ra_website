	<h2>Background</h2>		
 <p>In a crowded Insurance industry space, Brand Awareness is key to long-term success. Designing a brand awareness survey that takes into account the acculturation levels of Hispanics requires a blended approach to better understand key market areas. This Insurance company wanted to better understand key demographic market areas. By examining the performance of advertising within these areas, the client can examine the impact of ad campaigns and identify key best practices for marketing to Hispanics. After all, brand awareness research can improve the bottom line.</p>




<h2>Approach</h2>	
	
	<p>RAI met with the Insurance Company and their ad agency to develop a new Hispanic Brand awareness survey. We formed the questions and administered them using two different modes: Telephone and Internet. The telephone survey yielded 705 respondents, compared to the internet sample, which yielded 197 respondents. With nominal sampling costs, we were able to help the Insurance Company better understand the dynamics of Hispanic consumers in Southern CA and the Miami area to justify the cost of this research. </p>

	
	<h2>Results</h2>
	<p>RAI helped the Insurance Company gain valuable insights from the telephone and Internet samples on the effectiveness of the advertising campaign. The Insurance Company can now plan appropriately for media spend to target the Hispanic market better. The results and recommendations increased policy enrollments by 11% which benefited the bottom line. The research data suggested that age, language, education level, and advertising language preference all may vary based on telephone versus Internet sample. 

</p>
		
	
