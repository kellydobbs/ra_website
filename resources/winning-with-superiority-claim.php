	<h1> Regional Coffee Manufacturer Wins with Product Superiority Claim</h1>

	<p>
	Are you one of the nearly 64% of American adults who drink coffee daily (according to the National Coffee Association)? If so, you might enjoy this case study about how Research America employed the Claims Co-Creation Workshop process to help a regional coffee manufacturer expand in the ready-to-drink (RTD) coffee category.

</p>
    <span class="med-link"> The Client </span><br>
     RTD coffee has experienced double-digit volume growth over the past five years, according to the Beverage Marketing Corporation. Additionally, Fortune Business Insights values the global RTD coffee market at $19.05 billion in 2017. Further, the RTD coffee market will grow at a CAGR of 8.5% during the forecast period (2018-2025), reaching $36.60 billion by 2025. Our client, a regional coffee manufacturer, wanted to participate in this rapid growth with its RTD coffee products.
</p>
    
	<p><span class="med-link"> The Situation </span><br>

While major players, like Starbuck’s or Dunkin among other national brands, dominate the RTD coffee market, our client believed that they produced a better product and wanted to create a marketing campaign to entice RTD coffee drinkers to trial and adoption of their brand. However, they needed to establish a credible basis for this product superiority claim, as well as document the claim against potential competitive and legal challenges.</p>
<br>
	

	
	
<p align="center">
   <img src="../_img/RTD-or-canned-coffee.png"  alt=""/></p>
	<br>

	  <p><span class="med-link"> The Situation </span><br>
Research America recommended our Claims Co-Creation process to identify and validate potential claims. Pulling together all of the necessary individuals throughout our client’s organization and reaching out to relevant partners, we developed an integrated project team of marketers, creatives, lawyers, and R&D who would be involved in every step of our eight-step process. This ensured that everyone was volved in identifying, validating, and communicating the product superiority claim, understood what we learned from consumers, and why specific claims were advanced or rejected by the team. 

  </strong></p>
   
      
   	  <strong>Step 1: Consumer Input:</strong> We recruited 40 “expert consumers.” These were adult RTD coffee drinkers who were very creative and passionate about participating in the co-creation process. Our first evaluation was qualitative: three discussion groups and a taste test comparing our client’s brand to other national and regional brand products. In this step, we wanted to generate a wide variety of potential claims that were particularly important to consumers in choosing RTD coffee. All of the claims team members observed the qualitative research.
<br> <br>
	  
		<b>Step 2: Identify Potential Claims  </b>Quantitative research follows a defined process, especially in data collection. Surveys are programmed to allow the presentation of questions and stimuli in a specific order, whereas qualitative research can adapt to the information that is being gathered. If one approach is ineffective, a good qualitative researcher can immediately shift gears and seek to explore data in a new direction. If the researcher hears a promising thought, they can easily and quickly incorporate it into the discussion.<br> <br>
	  
	  
	<strong> 	Step 3: Creative Panels  </strong> QNext, we brought the “expert consumer panel” into an online discussion to rank and evaluate, provide feedback on opportunities for improvement for potential claims identified by the team. After receiving the results, the entire team further narrowed down the list to the most potentially effective – and provable – claims.<br><br>

	  
	<strong>	Step 4: Mapping of Claims, Evidence, and Reasoning  </strong>For each of the remaining claims, Legal and R&D had to evaluate the potential feasibility. Is it operationally possible for our client’s brand to credibly make this claim? Can we develop substantiation data that could protect us from competitive and legal challenges? Finally, a few claims made the cut.

	<br><br>
	
	<strong>Step 5: Creative and Messaging</strong> Brand Marketing, the Ad Agency, and Creatives then went to work to express the operational and defensible claims in a marketing campaign. Effectively communicating the product superiority claim and enticing product trial were the advertising goals.<br> 
	<br>
	
	
	<strong>Step 6: Prioritize Claims</strong>Once again, our client used their “expert consumer panel” to evaluate the advertising. The team selected the ad identified as most persuasive and credible for implementation.
<br><br>


	<strong>	Step 7: Substantiation Research </strong>  Finally, we selected a nationally representative sample of adult category users (n=600) to evaluate the product in a blind taste test asking the right questions to support the claims that were of interest:  blindly, which product do you prefer? How likely are you to buy this product? This research was able to provide the evidence needed to validate the leading claims: is our product better as claimed? Can we defend ourselves should our competitors object? 
.<br><br>
  
	
	<strong>	Step 8: Launch  </strong> The advertising campaign went live, and the results were monitored for effectiveness.
<br>
<br>
  
	
	<p><span class="med-link"> The Outcomes </span><br>
	
  Through Research America’s Ad Claims Co-Creation process, our client determined that they could legally – and accurately – claim that their RTD black coffee tastes better than other national brands. Substantiation research proved the claim: Our brand beat the national brands 2:1 in purchase intent.
Our client implemented a regional advertising campaign touting their brand as ““The best-tasting ready-to-drink black coffee you can buy!”, as well as supporting promotions to encourage trial. After six months, sales of our client’s RTD black coffee had increased by 6%. The team is now hard at work pursuing a product superiority claim for their RTD milk/sugar coffee beverages.
</p>
	
